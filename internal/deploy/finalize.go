// fstabMountOpts returns appropriate mount options for a given filesystem type
// and mountpoint. These are conservative production defaults for Rocky Linux 9.
func fstabMountOpts(fstype, mountpoint string) string {
	switch fstype {
	case "xfs":
		if mountpoint == "/" {
			return "defaults,noatime"
		}
		return "defaults"
	case "ext4":
		if mountpoint == "/" {
			return "defaults,noatime"
		}
		return "defaults"
	case "vfat", "fat32":
		return "defaults,uid=0,gid=0,umask=077,shortname=winnt"
	case "swap":
		return "defaults"
	default:
		return "defaults"
	}
}

// applyExtraMounts appends custom fstab entries from NodeConfig.ExtraMounts and
// optionally creates the mount point directories inside the deployed filesystem.
// It must be called after applyBootConfig so the base fstab already exists.
//
// Invalid entries are skipped with a warning rather than aborting the deployment,
// because a bad shared-storage entry should not prevent the node from booting.
func applyExtraMounts(ctx context.Context, mountRoot string, mounts []api.FstabEntry) error {
	if len(mounts) == 0 {
		return nil
	}

	log := logger()

	fstabPath := filepath.Join(mountRoot, "etc", "fstab")
	f, err := os.OpenFile(fstabPath, os.O_APPEND|os.O_WRONLY, 0644)
	if err != nil {
		return fmt.Errorf("applyExtraMounts: open fstab: %w", err)
	}
	defer f.Close()

	fmt.Fprintf(f, "\n# Additional mounts from clustr NodeConfig.ExtraMounts\n")

	written := 0
	for _, m := range mounts {
		if err := api.ValidateFstabEntry(m); err != nil {
			log.Warn().Err(err).Str("mount_point", m.MountPoint).
				Msg("finalize: skipping invalid extra fstab entry")
			continue
		}

		// Auto-create the mount point directory inside the deployed filesystem.
		if m.AutoMkdir {
			targetDir := filepath.Join(mountRoot, m.MountPoint)

			// Path traversal guard: the resolved absolute path must remain inside mountRoot.
			absTarget, err := filepath.Abs(targetDir)
			if err != nil || !strings.HasPrefix(absTarget, mountRoot+string(filepath.Separator)) {
				log.Warn().Str("mount_point", m.MountPoint).
					Msg("finalize: mount point escapes deployed filesystem root — skipping mkdir")
			} else if err := os.MkdirAll(absTarget, 0755); err != nil {
				// Non-fatal: the mount will fail at boot, but we still write the entry
				// so the operator can investigate rather than silently losing the config.
				log.Warn().Err(err).Str("path", absTarget).
					Msg("finalize: failed to create extra mount point (non-fatal)")
			} else {
				log.Info().Str("path", m.MountPoint).
					Msg("finalize: created mount point for extra mount")
			}
		}

		// Resolve mount options: default to "defaults" when empty, then auto-inject
		// _netdev for network filesystems that omit it (needed so systemd waits for
		// the network before attempting the mount).
		opts := m.Options
		if opts == "" {
			opts = "defaults"
		}
		if api.IsNetworkFS(m.FSType) && !strings.Contains(opts, "_netdev") {
			opts += ",_netdev"
			log.Info().Str("fs_type", m.FSType).Str("mount_point", m.MountPoint).
				Msg("finalize: auto-added _netdev to extra mount options for network filesystem")
		}

		if m.Comment != "" {
			fmt.Fprintf(f, "# %s\n", m.Comment)
		}
		// fstab format: source  mountpoint  fstype  options  dump  pass
		fmt.Fprintf(f, "%s\t%s\t%s\t%s\t%d\t%d\n",
			m.Source, m.MountPoint, m.FSType, opts, m.Dump, m.Pass)

		log.Info().
			Str("source", m.Source).
			Str("mount_point", m.MountPoint).
			Str("fs_type", m.FSType).
			Msg("finalize: wrote extra fstab entry")
		written++
	}

	log.Info().Int("count", written).Msg("finalize: extra fstab entries written")
	return nil
}

// getRaidArrayUUID returns the UUID of an assembled md RAID array in the
// colon-separated format used by dracut's rd.md.uuid= kernel cmdline parameter
// (e.g. "3c483b47:8a550722:e7c1e792:d426519c").
// It runs "mdadm --detail --export <device>" and extracts the MD_UUID field.
func getRaidArrayUUID(ctx context.Context, mdDev string) (string, error) {
	out, err := exec.CommandContext(ctx, "mdadm", "--detail", "--export", mdDev).Output()
	if err != nil {
		return "", fmt.Errorf("mdadm --detail --export %s: %w", mdDev, err)
	}
	for _, line := range strings.Split(string(out), "\n") {
		if strings.HasPrefix(line, "MD_UUID=") {
			return strings.TrimPrefix(line, "MD_UUID="), nil
		}
	}
	return "", fmt.Errorf("MD_UUID not found in mdadm --detail --export output for %s", mdDev)
}

// copyFile copies src to dst with the given permission bits.
// If dst's parent directory does not exist it is created.
func copyFile(src, dst string, perm os.FileMode) error {
	if err := os.MkdirAll(filepath.Dir(dst), 0o755); err != nil {
		return fmt.Errorf("copyFile: mkdir: %w", err)
	}
	in, err := os.Open(src)
	if err != nil {
		return fmt.Errorf("copyFile: open src: %w", err)
	}
	defer in.Close()

	out, err := os.OpenFile(dst, os.O_WRONLY|os.O_CREATE|os.O_TRUNC, perm)
	if err != nil {
		return fmt.Errorf("copyFile: open dst: %w", err)
	}
	defer out.Close()

	if _, err := io.Copy(out, in); err != nil {
		return fmt.Errorf("copyFile: copy: %w", err)
	}
	return nil
}

// writeLDAPConfig writes the node-side LDAP client configuration into the deployed
// filesystem rooted at mountRoot. This includes:
// (uses the package-level logger() like other finalize helpers)
//   - /etc/sssd/sssd.conf (sssd authentication configuration)
//   - /etc/openldap/ldap.conf (OpenLDAP client tools config)
//   - CA certificate in three locations (system trust, openldap, sssd)
//   - Enabling sssd.service in the systemd chroot
//   - Running update-ca-trust in the chroot
//
// The LDAPNodeConfig carries only the read-only service account credentials;
// the Directory Manager password is never present here.
func writeLDAPConfig(ctx context.Context, mountRoot string, ldapCfg *api.LDAPNodeConfig) error {
	if ldapCfg == nil {
		return nil
	}

	log := logger()

	// Derive domain name from base DN for sssd domain config.
	domain := ldapDomainFromBaseDN(ldapCfg.BaseDN)

	// ── sssd.conf ─────────────────────────────────────────────────────────────
	sssdDir := filepath.Join(mountRoot, "etc", "sssd")
	if err := os.MkdirAll(sssdDir, 0o700); err != nil {
		return fmt.Errorf("ldap finalize: mkdir sssd dir: %w", err)
	}

	// Create SSSD runtime directories required at boot.
	// These are normally created by the sssd RPM scriptlets but may be
	// missing if the image was scrubbed or built minimally.
	for _, dir := range []struct {
		path string
		mode os.FileMode
	}{
		{"var/log/sssd", 0o700},
		{"var/lib/sss", 0o711},
		{"var/lib/sss/db", 0o700},
		{"var/lib/sss/mc", 0o755},
		{"var/lib/sss/pipes", 0o755},
		{"var/lib/sss/pipes/private", 0o700},
		{"var/lib/sss/pubconf", 0o755},
		{"var/lib/sss/gpo_cache", 0o750},
		{"etc/sssd/conf.d", 0o700},
	} {
		fullPath := filepath.Join(mountRoot, dir.path)
		if err := os.MkdirAll(fullPath, dir.mode); err != nil {
			log.Warn().Err(err).Str("path", dir.path).Msg("finalize: create sssd runtime dir (non-fatal)")
		}
	}

	// Fix ownership of SSSD directories — sssd runs as the sssd user.
	// Use chroot chown so the UIDs resolve from the image's /etc/passwd.
	for _, d := range []string{"/var/log/sssd", "/var/lib/sss", "/etc/sssd"} {
		if out, err := exec.CommandContext(ctx, "chroot", mountRoot, "chown", "-R", "sssd:sssd", d).CombinedOutput(); err != nil {
			log.Warn().Err(err).Str("path", d).Str("output", string(out)).Msg("finalize: chown sssd dir (non-fatal)")
		}
	}

	sssdContent := renderSSSDConf(ldapCfg, domain)
	sssdPath := filepath.Join(sssdDir, "sssd.conf")
	if err := os.WriteFile(sssdPath, []byte(sssdContent), 0o600); err != nil {
		return fmt.Errorf("ldap finalize: write sssd.conf: %w", err)
	}

	// sssd PKI dir for the CA cert (some sssd builds look here).
	sssdPKIDir := filepath.Join(sssdDir, "pki")
	if err := os.MkdirAll(sssdPKIDir, 0o755); err != nil {
		return fmt.Errorf("ldap finalize: mkdir sssd pki dir: %w", err)
	}

	// ── ldap.conf ─────────────────────────────────────────────────────────────
	ldapConfDir := filepath.Join(mountRoot, "etc", "openldap")
	if err := os.MkdirAll(ldapConfDir, 0o755); err != nil {
		return fmt.Errorf("ldap finalize: mkdir openldap dir: %w", err)
	}
	ldapConfContent := renderLDAPConf(ldapCfg)
	if err := os.WriteFile(filepath.Join(ldapConfDir, "ldap.conf"), []byte(ldapConfContent), 0o644); err != nil {
		return fmt.Errorf("ldap finalize: write ldap.conf: %w", err)
	}

	// OpenLDAP certs dir for tools that don't use the system trust store.
	openldapCertsDir := filepath.Join(ldapConfDir, "certs")
	if err := os.MkdirAll(openldapCertsDir, 0o755); err != nil {
		return fmt.Errorf("ldap finalize: mkdir openldap certs dir: %w", err)
	}

	// ── CA certificate — three locations ─────────────────────────────────────
	caPEM := []byte(ldapCfg.CACertPEM)

	// 1. System trust anchor (update-ca-trust reads from here).
	sysAnchorDir := filepath.Join(mountRoot, "etc", "pki", "ca-trust", "source", "anchors")
	if err := os.MkdirAll(sysAnchorDir, 0o755); err != nil {
		return fmt.Errorf("ldap finalize: mkdir ca-trust anchors: %w", err)
	}
	if err := os.WriteFile(filepath.Join(sysAnchorDir, "clustr-ca.crt"), caPEM, 0o644); err != nil {
		return fmt.Errorf("ldap finalize: write system CA: %w", err)
	}

	// 2. OpenLDAP certs dir.
	if err := os.WriteFile(filepath.Join(openldapCertsDir, "clustr-ca.pem"), caPEM, 0o644); err != nil {
		return fmt.Errorf("ldap finalize: write openldap CA: %w", err)
	}

	// 3. sssd PKI dir.
	if err := os.WriteFile(filepath.Join(sssdPKIDir, "clustr-ca.pem"), caPEM, 0o644); err != nil {
		return fmt.Errorf("ldap finalize: write sssd CA: %w", err)
	}

	// ── update-ca-trust in chroot ─────────────────────────────────────────────
	// Run update-ca-trust inside the chroot so the system trust bundle is built
	// from the anchors we just wrote. Non-fatal — the anchor file is already in place.
	if out, err := exec.CommandContext(ctx, "chroot", mountRoot, "update-ca-trust", "extract").CombinedOutput(); err != nil {
		log.Warn().Err(err).Str("output", string(out)).
			Msg("finalize: update-ca-trust in chroot failed (non-fatal) — CA anchor file is still in place")
	}

	// ── Enable sssd.service in chroot ────────────────────────────────────────
	// sssd must start on first boot for user authentication to work.
	if out, err := exec.CommandContext(ctx, "chroot", mountRoot, "systemctl", "enable", "sssd.service").CombinedOutput(); err != nil {
		// Non-fatal: the unit file may not exist if sssd is not installed.
		log.Warn().Err(err).Str("output", string(out)).
			Msg("finalize: systemctl enable sssd.service in chroot failed (non-fatal) — sssd may not be installed in the image")
	}

	// ── Enable oddjobd for pam_mkhomedir (if available) ───────────────────────
	if out, err := exec.CommandContext(ctx, "chroot", mountRoot, "systemctl", "enable", "oddjobd.service").CombinedOutput(); err != nil {
		log.Warn().Err(err).Str("output", string(out)).
			Msg("finalize: systemctl enable oddjobd.service in chroot failed (non-fatal)")
	}

	// ── authselect: configure PAM and nsswitch.conf for sssd ─────────────────
	// GAP-NEW-1 (revised R4): Run `authselect select sssd with-mkhomedir --force`
	// ONLY when the LDAP server is reachable from the deploy environment.
	//
	// Problem: authselect rewrites the PAM stack to include pam_sss.so. If
	// sssd.service cannot reach the LDAP server at node boot time, sssd fails,
	// and pam_sss.so (still in the PAM stack) rejects SSH logins — even after
	// key authentication succeeds. The node becomes unreachable.
	//
	// Additional problem discovered in Round 4: when deploying onto a base image
	// that was previously deployed with `authselect select sssd`, the old sssd PAM
	// profile survives the image extraction. Even if we skip authselect here, the
	// stale pam_sss.so entries in the PAM stack cause the same lockout.
	//
	// Fix: TCP-probe the LDAP host:port before running authselect. A 3-second
	// timeout is long enough to rule out transient loss but short enough not to
	// block the deploy significantly. If the probe fails we ACTIVELY reset the
	// authselect profile to "minimal" (the distribution default, no pam_sss.so)
	// to ensure no stale sssd PAM entries can block logins.
	//
	// sssd.conf is still written — if LDAP comes up later, the operator reimages.
	ldapReachable := ldapServerReachable(ldapCfg.ServerURI, 3*time.Second)
	if !ldapReachable {
		log.Warn().
			Str("server_uri", ldapCfg.ServerURI).
			Msg("finalize: LDAP server unreachable — resetting authselect to minimal profile to prevent PAM lockout. " +
				"If LDAP is expected to be reachable at deploy time, check the ServerURI and network connectivity. " +
				"To activate sssd PAM integration, reimage after LDAP is confirmed reachable.")
		// Actively reset to minimal so stale sssd PAM entries from a prior deploy
		// cannot survive into the new image and block SSH logins.
		if out, err := exec.CommandContext(ctx, "chroot", mountRoot,
			"authselect", "select", "minimal", "--force",
		).CombinedOutput(); err != nil {
			log.Warn().Err(err).Str("output", string(out)).
				Msg("finalize: authselect select minimal in chroot failed (non-fatal) — PAM stack may contain stale sssd entries")
		} else {
			log.Info().Msg("finalize: authselect reset to minimal (LDAP unreachable — sssd PAM entries cleared)")
		}
	} else {
		// LDAP is reachable — wire up PAM/nsswitch for sssd.
		if out, err := exec.CommandContext(ctx, "chroot", mountRoot,
			"authselect", "select", "sssd", "with-mkhomedir", "--force",
		).CombinedOutput(); err != nil {
			log.Warn().Err(err).Str("output", string(out)).
				Msg("finalize: authselect in chroot failed (non-fatal) — PAM/nsswitch may need manual configuration")
		} else {
			log.Info().Msg("finalize: authselect sssd with-mkhomedir applied (LDAP reachable)")
		}
	}

	return nil
}

// ldapServerReachable performs a TCP dial to the host:port derived from
// serverURI (e.g. "ldaps://10.99.0.1:636") and returns true if the connection
// succeeds within the given timeout.
//
// It parses the URI with net/url. If the port is absent, it defaults to 636
// for ldaps:// and 389 for ldap://. An unparseable URI is treated as
// unreachable (returns false) with a warning log so authselect is safely
// skipped rather than causing a PAM lockout.
func ldapServerReachable(serverURI string, timeout time.Duration) bool {
	log := logger()
	u, err := url.Parse(serverURI)
	if err != nil {
		log.Warn().Err(err).Str("server_uri", serverURI).
			Msg("finalize: LDAP reachability check: could not parse ServerURI — treating as unreachable")
		return false
	}
	host := u.Hostname()
	port := u.Port()
	if port == "" {
		switch u.Scheme {
		case "ldaps":
			port = "636"
		default: // ldap or anything else
			port = "389"
		}
	}
	addr := net.JoinHostPort(host, port)
	conn, dialErr := net.DialTimeout("tcp", addr, timeout)
	if dialErr != nil {
		log.Warn().Err(dialErr).Str("addr", addr).
			Msg("finalize: LDAP reachability check: TCP dial failed — treating server as unreachable")
		return false
	}
	conn.Close()
	log.Info().Str("addr", addr).Msg("finalize: LDAP reachability check: server is reachable")
	return true
}

// writeSlurmConfig writes Slurm config files, scripts, and service enablement into
// the deployed filesystem rooted at mountRoot. All operations are non-fatal —
// errors are logged as warnings so a Slurm config problem never aborts a deploy.
//
// What it does:
//   - Creates /etc/slurm/, /etc/munge/, /var/spool/slurm{d,ctld}/, /var/log/slurm/,
//     /var/log/munge/, /var/run/munge/
//   - Writes each rendered config file from SlurmNodeConfig.Configs
//   - Writes each script from SlurmNodeConfig.Scripts (mode 0755)
//   - Writes munge.key if present (mode 0400, owner munge:munge)
//   - Enables role-appropriate systemd services via chroot systemctl enable
//
// nodeID identifies the target node (used for audit log entries on install failure).
// auditFn is optional; when non-nil it is called on dnf install failure with the
// full error detail so the server can write an audit_log record.
func writeSlurmConfig(ctx context.Context, mountRoot, nodeID string, slurmCfg *api.SlurmNodeConfig, auditFn SlurmInstallAuditFn) error {
	if slurmCfg == nil {
		return nil
	}

	log := logger()

	// Determine which roles this node has.
	// Prefer the explicit Roles field populated by manager.NodeConfig().
	// Fall back to inferring from config file names for backward compatibility
	// with payloads that predate the Roles field.
	hasSlurmdbd := false
	hasGres := false
	hasSlurmConf := false

	if len(slurmCfg.Roles) > 0 {
		// Roles field is authoritative — set by manager.NodeConfig() from the DB.
		// GAP-NEW-3: do NOT fall through to the config-file heuristic when roles
		// are present. slurmdbd.conf is included in the payload for all nodes
		// (it is in defaultManagedFiles so manager.NodeConfig returns it regardless
		// of role) — without this guard the config-file loop below would set
		// hasSlurmdbd=true on every compute node, causing slurmctld to be installed
		// and enabled on workers.
		for _, r := range slurmCfg.Roles {
			switch r {
			case "controller":
				// hasSlurmdbd gates slurmctld installation/enable (confusingly named,
				// but kept for backward compat — see installSlurmInChroot comment).
				hasSlurmdbd = true
			case "worker", "compute":
				// "worker" is the canonical API role value; "compute" accepted for
				// backward compatibility with payloads written before the rename.
				hasGres = true
			}
		}

		// Detect hasSlurmConf from the config list even when roles are present —
		// this flag only gates a write, not a package install or service enable.
		for _, cf := range slurmCfg.Configs {
			if cf.Filename == "slurm.conf" {
				hasSlurmConf = true
			}
		}
	} else {
		// No roles assigned yet — fall back to inferring from config file names for
		// backward compatibility with payloads that predate the Roles field.
		// WARNING: this path can produce incorrect results when slurmdbd.conf is
		// included in a compute-node payload (it is in defaultManagedFiles). It is
		// kept only as a backward-compat escape hatch; new deployments always have
		// roles populated by manager.NodeConfig().
		for _, cf := range slurmCfg.Configs {
			switch cf.Filename {
			case "slurmdbd.conf":
				hasSlurmdbd = true
			case "gres.conf":
				hasGres = true
			case "slurm.conf":
				hasSlurmConf = true
			}
		}
	}
	_ = hasSlurmConf // always written if present

	// ── Auto-install Slurm packages (Option B: operator-provided repo URL) ───
	// When SlurmRepoURL is set, clustr adds the repo and installs slurm + munge
	// inside the chroot before writing config files.  This makes the deploy
	// truly turnkey for images (e.g. Rocky 10) where EPEL does not carry Slurm.
	//
	// The operator provides the URL at module-enable time via:
	//   POST /api/v1/modules/slurm/enable  {"slurm_repo_url": "https://..."}
	// and it is stored in slurm_module_config.slurm_repo_url.
	//
	// Package selection by role:
	//   controller / dbd → slurm slurm-slurmctld munge
	//   compute (gres)   → slurm slurm-slurmd munge
	//   neither (fallback)→ slurm munge
	if slurmCfg.SlurmRepoURL != "" {
		// installSlurmInChroot runs 'dnf install slurm munge' inside the chroot.
		// The munge and slurm RPM %pre scriptlets create the munge:munge and
		// slurm:slurm system users — this is the primary path that ensures those
		// accounts exist before the chown block below runs.
		//
		// Decode the GPG key from base64 if present (populated by the slurm manager
		// for the clustr-builtin path; empty for operator-provided custom URLs).
		var gpgKeyBytes []byte
		if slurmCfg.GPGKey != "" {
			if decoded, err := base64.StdEncoding.DecodeString(slurmCfg.GPGKey); err == nil {
				gpgKeyBytes = decoded
			} else {
				log.Warn().Err(err).Msg("finalize slurm: auto-install: failed to decode GPGKey (non-fatal, falling back to gpgcheck=0)")
			}
		}
		installSlurmInChroot(ctx, mountRoot, nodeID, slurmCfg.SlurmRepoURL, hasSlurmdbd, hasGres, auditFn, gpgKeyBytes)
	} else {
		// No SlurmRepoURL — slurm + munge must already be installed in the base
		// image. The chown steps below require slurm:slurm and munge:munge to
		// exist in the chroot's /etc/passwd. If those accounts are absent (e.g.
		// the base image was captured before slurm was installed), the chown will
		// log a non-fatal warning and slurmctld/munged will fail on first boot.
		// Ensure one of the following is true:
		//   1. Base image was captured with slurm + munge pre-installed (accounts
		//      created by RPM scriptlets at install time).
		//   2. The SystemAccounts module is configured with slurm (UID 202) and
		//      munge (UID 1010) entries — these are injected by injectSystemAccounts
		//      which runs after this function in applyNodeConfig.
		log.Info().
			Msg("finalize slurm: no SlurmRepoURL — slurm/munge packages must already exist in base image; " +
				"if slurm:slurm or munge:munge users are missing, chown will warn and daemons will fail on boot. " +
				"Add slurm_repo_url via POST /api/v1/modules/slurm/enable or pre-install slurm+munge in the captured image.")
	}

	// ── Create directories ────────────────────────────────────────────────────
	dirs := []struct {
		path string
		mode os.FileMode
	}{
		{"etc/slurm", 0o755},
		{"etc/munge", 0o700},
		{"var/log/slurm", 0o755},
		{"var/log/munge", 0o755},
		{"var/run/munge", 0o755},
	}
	if hasGres || !hasSlurmdbd {
		// compute node needs slurmd spool dir
		dirs = append(dirs, struct {
			path string
			mode os.FileMode
		}{"var/spool/slurmd", 0o755})
	}
	if hasSlurmdbd {
		// controller/dbd needs slurmctld spool dir
		dirs = append(dirs, struct {
			path string
			mode os.FileMode
		}{"var/spool/slurmctld", 0o755})
	}

	// NEW-GAP-14: Wipe slurmctld and slurmd spool directories before (re)creating
	// them.  A base image captured after a prior Slurm install may contain stale
	// state files — most critically /var/spool/slurmctld/clustername — which
	// causes slurmctld to FATAL on first boot with "CLUSTER NAME MISMATCH" because
	// the on-disk name differs from the cluster_name in slurm.conf.
	//
	// We wipe the directories unconditionally for any node that is receiving a
	// Slurm role.  This is safe because we always do a clean reimage: the node's
	// disk is freshly written from the base image, so there is no in-service state
	// to preserve.  After the wipe we recreate the directory at the correct
	// ownership (slurm:slurm) so slurmctld/slurmd start cleanly on first boot.
	spoolDirsToWipe := []string{}
	if hasGres || !hasSlurmdbd {
		spoolDirsToWipe = append(spoolDirsToWipe, "var/spool/slurmd")
	}
	if hasSlurmdbd {
		spoolDirsToWipe = append(spoolDirsToWipe, "var/spool/slurmctld")
	}
	for _, sp := range spoolDirsToWipe {
		fullPath := filepath.Join(mountRoot, sp)
		if _, err := os.Stat(fullPath); err == nil {
			// Directory exists — remove all contents to clear stale state files.
			if err := os.RemoveAll(fullPath); err != nil {
				log.Warn().Err(err).Str("path", sp).
					Msg("finalize slurm: wipe spool dir failed (non-fatal) — stale state files may remain")
			} else {
				log.Info().Str("path", sp).
					Msg("finalize slurm: wiped spool dir to remove stale state (prevents clustername mismatch on first boot)")
			}
		}
	}

	for _, d := range dirs {
		fullPath := filepath.Join(mountRoot, d.path)
		if err := os.MkdirAll(fullPath, d.mode); err != nil {
			log.Warn().Err(err).Str("path", d.path).Msg("finalize slurm: mkdir (non-fatal)")
		}
	}

	// ── Write config files ────────────────────────────────────────────────────
	fileModeMap := map[string]os.FileMode{
		"slurmdbd.conf": 0o600,
	}
	for _, cf := range slurmCfg.Configs {
		mode := os.FileMode(0o644)
		if m, ok := fileModeMap[cf.Filename]; ok {
			mode = m
		}
		destPath := filepath.Join(mountRoot, "etc", "slurm", cf.Filename)
		if err := os.WriteFile(destPath, []byte(cf.Content), mode); err != nil {
			log.Warn().Err(err).Str("filename", cf.Filename).Msg("finalize slurm: write config file (non-fatal)")
			continue
		}
		log.Info().Str("filename", cf.Filename).Str("mode", cf.FileMode).
			Msg("finalize slurm: wrote config file")
	}

	// ── Write scripts ─────────────────────────────────────────────────────────
	for _, sf := range slurmCfg.Scripts {
		if sf.DestPath == "" {
			continue
		}
		// Scripts may live anywhere (e.g. /etc/slurm/prolog.sh, /usr/local/sbin/...).
		destPath := filepath.Join(mountRoot, sf.DestPath)
		// Ensure parent directory exists.
		if err := os.MkdirAll(filepath.Dir(destPath), 0o755); err != nil {
			log.Warn().Err(err).Str("dest_path", sf.DestPath).Msg("finalize slurm: mkdir for script (non-fatal)")
		}
		if err := os.WriteFile(destPath, []byte(sf.Content), 0o755); err != nil {
			log.Warn().Err(err).Str("script_type", sf.ScriptType).Str("dest_path", sf.DestPath).
				Msg("finalize slurm: write script (non-fatal)")
			continue
		}
		log.Info().Str("script_type", sf.ScriptType).Str("dest_path", sf.DestPath).
			Msg("finalize slurm: wrote script")
	}

	// ── Write slurmctld systemd drop-in (cgroup v2 delegation) ──────────────
	// R2-G2: On systemd + cgroup v2, a service running as a non-root user
	// (User=slurm in the upstream slurmctld.service) cannot manage its own
	// cgroup subtree unless systemd has delegated that subtree to it.  Without
	// Delegate=yes, slurmscriptd (fork+exec'd by slurmctld via /proc/self/exe)
	// crashes immediately at init with:
	//   slurmscriptd failed to send return code: No such file or directory
	// because the cgroup.controllers write is refused by the kernel.
	//
	// Fix: write a drop-in that adds Delegate=yes while keeping User=slurm
	// (principle of least privilege preserved).  The drop-in is written to
	// /etc/systemd/system/slurmctld.service.d/clustr.conf so it survives
	// package updates that might overwrite the base unit file.
	//
	// This only applies to controller nodes (hasSlurmdbd).  Compute nodes run
	// slurmd which does not fork slurmscriptd and is not affected.
	if hasSlurmdbd {
		dropInDir := filepath.Join(mountRoot, "etc", "systemd", "system", "slurmctld.service.d")
		if err := os.MkdirAll(dropInDir, 0o755); err != nil {
			log.Warn().Err(err).Str("path", dropInDir).
				Msg("finalize slurm: mkdir slurmctld drop-in dir (non-fatal)")
		} else {
			const slurmctldDropIn = "[Service]\n# clustr: delegate cgroup v2 subtree to the slurm user so that\n# slurmscriptd (fork+exec'd by slurmctld) can manage task/cgroup and\n# proctrack/cgroup plugins without running slurmctld as root.\nDelegate=yes\n"
			dropInPath := filepath.Join(dropInDir, "clustr.conf")
			if err := os.WriteFile(dropInPath, []byte(slurmctldDropIn), 0o644); err != nil {
				log.Warn().Err(err).Str("path", dropInPath).
					Msg("finalize slurm: write slurmctld drop-in (non-fatal) — slurmctld may fail to start on cgroup v2 systems")
			} else {
				log.Info().Str("path", dropInPath).
					Msg("finalize slurm: wrote slurmctld Delegate=yes drop-in for cgroup v2 compatibility")
			}
		}
	}

	// ── Write munge key ───────────────────────────────────────────────────────
	// NEW-GAP-17: The munge key was never included in the deploy payload, so
	// munged could not start — leaving the node in a degraded state where Slurm
	// auth fails on every srun/scontrol call.
	//
	// The key is delivered as base64-encoded bytes in SlurmNodeConfig.MungeKey.
	// We decode it and write it to /etc/munge/munge.key with mode 0400 here.
	// The existing chown loop below handles munge:munge ownership.
	if slurmCfg.MungeKey != "" {
		rawMungeKey, mkDecErr := base64.StdEncoding.DecodeString(slurmCfg.MungeKey)
		if mkDecErr != nil {
			log.Warn().Err(mkDecErr).Msg("finalize slurm: munge key base64 decode failed (non-fatal) — munge will not start")
		} else {
			mungeKeyPath := filepath.Join(mountRoot, "etc", "munge", "munge.key")
			if mkWriteErr := os.WriteFile(mungeKeyPath, rawMungeKey, 0o400); mkWriteErr != nil {
				log.Warn().Err(mkWriteErr).Msg("finalize slurm: write munge.key (non-fatal) — munge will not start")
			} else {
				log.Info().Str("path", mungeKeyPath).Int("bytes", len(rawMungeKey)).
					Msg("finalize slurm: wrote munge.key")
			}
		}
	} else {
		log.Warn().Msg("finalize slurm: no munge key in deploy payload — /etc/munge/munge.key not written; munge will fail to start (run POST /slurm/munge-key/generate if key is missing)")
	}

	// ── Fix ownership of slurm directories ───────────────────────────────────
	for _, d := range []string{"/etc/slurm", "/var/log/slurm", "/var/spool/slurmd", "/var/spool/slurmctld"} {
		fullPath := filepath.Join(mountRoot, d)
		if _, err := os.Stat(fullPath); os.IsNotExist(err) {
			continue
		}
		if out, err := exec.CommandContext(ctx, "chroot", mountRoot, "chown", "-R", "slurm:slurm", d).CombinedOutput(); err != nil {
			log.Warn().Err(err).Str("path", d).Str("output", string(out)).
				Msg("finalize slurm: chown slurm dir (non-fatal) — slurm user may not exist in image")
		}
	}

	// ── Fix ownership of munge directories ───────────────────────────────────
	for _, d := range []string{"/etc/munge", "/var/log/munge", "/var/run/munge"} {
		if out, err := exec.CommandContext(ctx, "chroot", mountRoot, "chown", "-R", "munge:munge", d).CombinedOutput(); err != nil {
			log.Warn().Err(err).Str("path", d).Str("output", string(out)).
				Msg("finalize slurm: chown munge dir (non-fatal) — munge user may not exist in image")
		}
	}

	// ── Enable services in chroot ─────────────────────────────────────────────
	// GAP-14: Only enable a service when its binary is present in the chroot.
	// Without this guard, `systemctl enable slurmd` on an image that has no
	// slurmd binary creates a broken symlink and puts the unit in a degraded
	// state on first boot — which causes PAM to terminate SSH sessions after key
	// acceptance, making every freshly deployed node unreachable via SSH.
	//
	// svcBinaryMap maps systemd unit → the binary (relative to chroot root)
	// whose existence gates the enable call. If the binary is absent we log INFO
	// and skip — the node boots cleanly without that service.
	svcBinaryMap := map[string]string{
		"munge.service":    "usr/sbin/munged",
		"slurmd.service":   "usr/sbin/slurmd",
		"slurmctld.service": "usr/sbin/slurmctld",
		"slurmdbd.service": "usr/sbin/slurmdbd",
	}

	// Determine which services to enable based on node role.
	services := []string{}
	// munge: enable only if munged is present (required by all Slurm daemons,
	// but skip gracefully on images that have no Slurm at all).
	services = append(services, "munge.service")
	if hasGres {
		// Compute node.
		services = append(services, "slurmd.service")
	}
	if hasSlurmdbd {
		// Controller or DBD node.
		services = append(services, "slurmctld.service", "slurmdbd.service")
	}

	for _, svc := range services {
		// Check binary existence in chroot before enabling the service.
		// This prevents systemctl enable from creating dangling symlinks when
		// slurm/munge are not installed in the image.
		if binRel, known := svcBinaryMap[svc]; known {
			if !fileExists(filepath.Join(mountRoot, binRel)) {
				log.Info().Str("service", svc).Str("binary", binRel).
					Msg("finalize slurm: skipping systemctl enable — binary not found in image (Slurm not installed)")
				continue
			}
		}
		if out, err := exec.CommandContext(ctx, "chroot", mountRoot, "systemctl", "enable", svc).CombinedOutput(); err != nil {
			log.Warn().Err(err).Str("service", svc).Str("output", string(out)).
				Msgf("finalize slurm: systemctl enable %s (non-fatal) — service may not be installed", svc)
		} else {
			log.Info().Str("service", svc).Msg("finalize slurm: enabled service")
		}
	}

	return nil
}

