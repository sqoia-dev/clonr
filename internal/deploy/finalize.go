// applyNodeConfig writes all node-specific identity into the deployed filesystem
// rooted at mountRoot. This function is called by both FilesystemDeployer and
// BlockDeployer after the image is on disk.
//
// Order of operations:
//  1. Hostname
//  2. Network config (NetworkManager keyfiles)
//  3. SSH authorized_keys for root
//  4. /etc/fstab UUID update
//  5. Kernel args (GRUB)
//  6. BMC / IPMI network and credentials (if cfg.BMC is set)
//  7. InfiniBand / IPoIB config (if cfg.IBConfig is set)
//  8. System accounts (groups then users, idempotent via getent check in chroot)
//  9. Network config (bond NM keyfiles, IPoIB keyfile, opensm.conf if head node)
func applyNodeConfig(ctx context.Context, cfg api.NodeConfig, mountRoot string) error {
	log := logger()

	log.Info().Str("hostname", cfg.Hostname).Msg("finalize: writing /etc/hostname")
	if err := writeHostname(mountRoot, cfg.Hostname, cfg.FQDN); err != nil {
		return fmt.Errorf("finalize: hostname: %w", err)
	}
	log.Info().Str("hostname", cfg.Hostname).Msg("finalize: wrote /etc/hostname")

	if len(cfg.ClusterHosts) > 0 {
		log.Info().Int("entries", len(cfg.ClusterHosts)).Msg("finalize: writing cluster hosts to /etc/hosts")
		if err := writeClusterHosts(mountRoot, cfg.ClusterHosts); err != nil {
			// Non-fatal: LDAP and other services may still work via broadcast or
			// manual /etc/hosts. Log prominently so the operator knows to investigate.
			log.Warn().Err(err).Msg("WARNING: finalize: cluster hosts write failed (non-fatal)")
		} else {
			log.Info().Int("entries", len(cfg.ClusterHosts)).Msg("finalize: wrote cluster hosts to /etc/hosts")
		}
	}

	log.Info().Int("interfaces", len(cfg.Interfaces)).Msg("finalize: writing NetworkManager connection profiles")
	if err := writeNetworkConfig(mountRoot, cfg.Interfaces); err != nil {
		return fmt.Errorf("finalize: network: %w", err)
	}
	for _, iface := range cfg.Interfaces {
		log.Info().Str("interface", iface.Name).Str("ip", iface.IPAddress).
			Msgf("finalize: wrote /etc/NetworkManager/system-connections/%s.nmconnection", iface.Name)
	}

	// Inject a high-priority DHCP fallback profile so clustr-verify-boot.service
	// has network connectivity to phone home even when the node has static NM
	// profiles that may not cover the interface that comes up first after reboot.
	// autoconnect-priority=100 ensures this profile wins over the static config
	// if needed; it is intentionally generic (no interface-name pinning) so it
	// works on any hardware with any NIC naming scheme.
	if err := writeClustrDHCPProfile(mountRoot); err != nil {
		// Non-fatal: static NM profiles may still provide connectivity.
		log.Warn().Err(err).Msg("WARNING finalize: could not write clustr-dhcp NM profile (non-fatal)")
	} else {
		log.Info().Msg("finalize: wrote clustr-dhcp NetworkManager DHCP fallback profile")
	}

	if len(cfg.SSHKeys) > 0 {
		log.Info().Int("keys", len(cfg.SSHKeys)).Msg("finalize: writing /root/.ssh/authorized_keys")
		if err := writeSSHKeys(mountRoot, cfg.SSHKeys); err != nil {
			return fmt.Errorf("finalize: ssh keys: %w", err)
		}
		log.Info().Int("keys", len(cfg.SSHKeys)).Msg("finalize: wrote /root/.ssh/authorized_keys")
	}

	// Ensure /etc/shadow exists with a valid root entry.
	// Some base images (e.g. QCOW2-derived tarballs) omit /etc/shadow because
	// the image builder runs as non-root and cannot preserve the 640/root:shadow
	// ownership. Without /etc/shadow, pam_unix.so account phase returns
	// PAM_AUTH_ERR for ALL users (the root password field in /etc/passwd is "x",
	// signalling shadow use), causing sshd to log "Access denied for user root
	// by PAM account configuration" and close the connection after key auth.
	//
	// We write a minimal shadow with a locked password ("!!") for root and any
	// system accounts present in /etc/passwd that would normally appear in shadow.
	// A locked password prevents password-based login but does NOT block pubkey auth.
	if err := ensureShadowFile(mountRoot); err != nil {
		// Non-fatal: log prominently, let the deploy continue.
		// If shadow is genuinely missing, SSH will fail — the operator must
		// investigate the base image.
		log.Warn().Err(err).Msg("WARNING: finalize: could not ensure /etc/shadow (non-fatal) — SSH logins may be blocked by pam_unix if shadow is missing")
	}

	if cfg.KernelArgs != "" {
		log.Info().Str("args", cfg.KernelArgs).Msg("finalize: applying kernel args to /etc/default/grub")
		if err := applyKernelArgs(ctx, mountRoot, cfg.KernelArgs); err != nil {
			// Non-fatal: the GRUB config file edit may have succeeded even if
			// grub2-mkconfig failed in the chroot. Log prominently so operators
			// know to verify the bootloader configuration manually.
			log.Warn().Err(err).Msg("WARNING: finalize: kernel args update failed (non-fatal) — manual intervention may be required")
		} else {
			log.Info().Str("args", cfg.KernelArgs).Msg("finalize: kernel args applied, ran grub2-mkconfig")
		}
	}

	// BMC / IPMI — configure local BMC network and credentials.
	// This operates on the physical BMC directly (not the chroot), so it is
	// done here rather than inside the deployed filesystem.
	if cfg.BMC != nil {
		log.Info().Str("bmc_ip", cfg.BMC.IPAddress).Msg("finalize: configuring BMC via ipmitool")
		if err := applyBMCConfig(ctx, cfg.BMC); err != nil {
			// Non-fatal: BMC configuration failure should not abort a deployment.
			// The operator can manually configure the BMC afterward.
			log.Warn().Err(err).Msg("WARNING: finalize: BMC configuration failed (non-fatal)")
		} else {
			log.Info().Str("bmc_ip", cfg.BMC.IPAddress).Msg("finalize: BMC configured")
		}
	}

	// InfiniBand / IPoIB — write udev rules and NetworkManager profiles into
	// the deployed filesystem so IB interfaces come up correctly on first boot.
	if len(cfg.IBConfig) > 0 {
		log.Info().Int("devices", len(cfg.IBConfig)).Msg("finalize: writing InfiniBand/IPoIB configuration")
		if err := writeIBConfig(mountRoot, cfg.IBConfig); err != nil {
			return fmt.Errorf("finalize: ib config: %w", err)
		}
		log.Info().Int("devices", len(cfg.IBConfig)).Msg("finalize: InfiniBand/IPoIB configuration written")
	}

	// LDAP module — write sssd.conf, ldap.conf, and CA bundle into the deployed
	// filesystem so the node can authenticate users via the clustr LDAP server.
	if cfg.LDAPConfig != nil {
		log.Info().Str("base_dn", cfg.LDAPConfig.BaseDN).Msg("finalize: writing LDAP client configuration")
		if err := writeLDAPConfig(ctx, mountRoot, cfg.LDAPConfig); err != nil {
			// Non-fatal: LDAP config failure should not abort a deployment.
			// The node will boot without LDAP; operator can re-image to add LDAP.
			log.Warn().Err(err).Msg("WARNING: finalize: LDAP config failed (non-fatal)")
		} else {
			log.Info().Msg("finalize: LDAP client configuration written")
		}
	}

	// Sudoers drop-in — write /etc/sudoers.d/<group_cn> so LDAP group members
	// can run sudo on the deployed node. SSSD resolves group membership at sudo time.
	// Only written when both LDAPConfig and SudoersConfig are present.
	if cfg.LDAPConfig != nil && cfg.SudoersConfig != nil {
		if err := writeSudoersDropin(mountRoot, cfg.SudoersConfig); err != nil {
			log.Warn().Err(err).Msg("WARNING: finalize: sudoers drop-in failed (non-fatal)")
		} else {
			log.Info().Str("group", cfg.SudoersConfig.GroupCN).Msg("finalize: sudoers drop-in written")
		}
	}

	// Slurm module — write slurm.conf, gres.conf, etc., enable Slurm/munge services.
	if cfg.SlurmConfig != nil {
		log.Info().Str("cluster", cfg.SlurmConfig.ClusterName).
			Int("configs", len(cfg.SlurmConfig.Configs)).
			Msg("finalize: writing Slurm configuration")
		// auditFn is nil here — applyNodeConfig is a low-level deploy function
		// that does not have direct access to the audit service. Callers that
		// want audit logging should use WriteSlurmConfigWithAudit instead.
		if err := writeSlurmConfig(ctx, mountRoot, cfg.ID, cfg.SlurmConfig, nil); err != nil {
			// Non-fatal: Slurm config failure should not abort a deployment.
			// The node will boot without Slurm; operator can re-image to add Slurm.
			log.Warn().Err(err).Msg("WARNING: finalize: Slurm config failed (non-fatal)")
		} else {
			log.Info().Msg("finalize: Slurm configuration written")
		}
	}

	// Step 8: System accounts — inject local POSIX accounts and groups into the
	// deployed filesystem before first boot. Services (slurm, munge, nfs) start
	// before sssd; accounts must exist locally.
	if cfg.SystemAccounts != nil &&
		(len(cfg.SystemAccounts.Groups) > 0 || len(cfg.SystemAccounts.Accounts) > 0) {
		log.Info().
			Int("groups", len(cfg.SystemAccounts.Groups)).
			Int("accounts", len(cfg.SystemAccounts.Accounts)).
			Msg("finalize: injecting system accounts")
		if err := injectSystemAccounts(ctx, mountRoot, cfg.SystemAccounts); err != nil {
			// Non-fatal: log prominently so operator can remediate without aborting
			// the deploy. Node boots without the accounts; re-image to fix.
			log.Warn().Err(err).Msg("WARNING: finalize: system accounts injection failed (non-fatal)")
		} else {
			log.Info().Msg("finalize: system accounts injected")
		}
	}

	// Step 9: Network config — write NM keyfiles for bonds, VLANs, and IPoIB;
	// optionally inject opensm.conf and enable opensm.service.
	if cfg.NetworkConfig != nil {
		log.Info().
			Int("bonds", len(cfg.NetworkConfig.Bonds)).
			Bool("ib", cfg.NetworkConfig.IB != nil).
			Bool("opensm", cfg.NetworkConfig.OpenSMConf != "").
			Msg("finalize: injecting network config")
		if err := injectNetworkConfig(ctx, mountRoot, cfg.NetworkConfig); err != nil {
			// Non-fatal: node boots with whatever NM keyfiles the image already
			// has. Admin can reimage to fix.
			log.Warn().Err(err).Msg("WARNING: finalize: network config injection failed (non-fatal)")
		} else {
			log.Info().Msg("finalize: network config injected")
		}
	}

	// Step 10: Proxmox qemu-guest-agent — install and enable the guest agent so
	// Proxmox can query the VM for its actual network info (IPs, interfaces),
	// enabling the Proxmox UI network tab to auto-populate.
	if cfg.PowerProvider != nil && cfg.PowerProvider.Type == "proxmox" {
		log.Info().Msg("finalize: installing qemu-guest-agent for Proxmox-backed node")
		if err := installQEMUGuestAgent(ctx, mountRoot); err != nil {
			// Non-fatal: node boots without the agent; operator can install manually.
			log.Warn().Err(err).Msg("WARNING: finalize: qemu-guest-agent install failed (non-fatal)")
		} else {
			log.Info().Msg("finalize: qemu-guest-agent installed and enabled")
		}
	}

	return nil
}

// applyBMCConfig configures the local BMC via ipmitool.
// This targets the physical BMC of the node being finalized, not the chroot.
func applyBMCConfig(ctx context.Context, bmc *api.BMCNodeConfig) error {
	c := &ipmi.Client{} // local BMC — no host flags

	bmcCfg := ipmi.BMCConfig{
		Channel:   1,
		IPAddress: bmc.IPAddress,
		Netmask:   bmc.Netmask,
		Gateway:   bmc.Gateway,
		IPSource:  "static",
	}
	if err := c.SetBMCNetwork(ctx, bmcCfg); err != nil {
		return fmt.Errorf("set bmc network: %w", err)
	}

	if bmc.Username != "" && bmc.Password != "" {
		// Use user slot 2 — slot 1 is typically the reserved anonymous user.
		if err := c.SetBMCUser(ctx, 2, bmc.Username, bmc.Password); err != nil {
			return fmt.Errorf("set bmc user: %w", err)
		}
	}
	return nil
}

// installQEMUGuestAgent detects the distro family in rootDir, installs the
// qemu-guest-agent package via the appropriate package manager, and enables the
// service via systemctl. It is called during finalization when a node's power
// provider is Proxmox, allowing Proxmox to query live network information from
// the VM and auto-populate the Proxmox UI network tab.
//
// Distro detection:
//   - /etc/redhat-release present → RHEL/Rocky/Alma family (dnf)
//   - /etc/debian_version present → Debian/Ubuntu family (apt-get)
//   - Neither found → warn and skip (non-fatal at the call site)
//
// If /usr/bin/qemu-ga or /usr/sbin/qemu-ga already exists in the rootfs the
// package install step is skipped — the chroot has no network so dnf/apt-get
// would always fail when the binary is already present from the base image.
func installQEMUGuestAgent(ctx context.Context, rootDir string) error {
	log := logger()

	// If the binary is already present (baked into the base image), skip the
	// network-dependent package install entirely and go straight to enabling
	// the service.
	alreadyInstalled := fileExists(filepath.Join(rootDir, "usr", "bin", "qemu-ga")) ||
		fileExists(filepath.Join(rootDir, "usr", "sbin", "qemu-ga"))
	if alreadyInstalled {
		log.Info().Str("root", rootDir).
			Msg("finalize: qemu-guest-agent binary already present — skipping package install")
	} else {
		var pkgMgr string
		switch {
		case fileExists(filepath.Join(rootDir, "etc", "redhat-release")):
			pkgMgr = "dnf"
		case fileExists(filepath.Join(rootDir, "etc", "debian_version")):
			pkgMgr = "apt-get"
		default:
			log.Warn().Str("root", rootDir).
				Msg("finalize: qemu-guest-agent: could not detect distro family — skipping install")
			return nil
		}

		log.Info().Str("pkg_mgr", pkgMgr).Msg("finalize: installing qemu-guest-agent in chroot")
		if out, err := exec.CommandContext(ctx, "chroot", rootDir,
			pkgMgr, "install", "-y", "qemu-guest-agent",
		).CombinedOutput(); err != nil {
			return fmt.Errorf("install qemu-guest-agent via %s: %w\noutput: %s", pkgMgr, err, string(out))
		}
	}

	if out, err := exec.CommandContext(ctx, "chroot", rootDir,
		"systemctl", "enable", "qemu-guest-agent",
	).CombinedOutput(); err != nil {
		return fmt.Errorf("systemctl enable qemu-guest-agent: %w\noutput: %s", err, string(out))
	}

	return nil
}

// fileExists reports whether the file at path exists (is stat-able).
func fileExists(path string) bool {
	_, err := os.Stat(path)
	return err == nil
}

// writeIBConfig writes IPoIB NetworkManager connection profiles and udev rules
// for each InfiniBand device into the deployed filesystem at mountRoot.
func writeIBConfig(mountRoot string, ibCfgs []api.IBInterfaceConfig) error {
	nmDir := filepath.Join(mountRoot, "etc", "NetworkManager", "system-connections")
	if err := os.MkdirAll(nmDir, 0o700); err != nil {
		return fmt.Errorf("mkdir NM connections: %w", err)
	}

	for _, ib := range ibCfgs {
		if err := writeIPoIBProfile(nmDir, ib); err != nil {
			return fmt.Errorf("ib device %s: %w", ib.DeviceName, err)
		}
	}
	return nil
}

// writeIPoIBProfile writes a NetworkManager keyfile for an IPoIB interface.
// The interface name is derived from the IB device name (e.g. mlx5_0 → ib0).
func writeIPoIBProfile(nmDir string, ib api.IBInterfaceConfig) error {
	// IPoIB interface naming: mlx5_0→ib0, mlx5_1→ib1, hfi1_0→ib0, etc.
	// We use the device name directly as the NM connection id and interface-name.
	ifaceName := ibDeviceToIPoIBName(ib.DeviceName)

	// Validate the derived interface name before using it as a filename component.
	if !ifaceNameRe.MatchString(ifaceName) {
		return fmt.Errorf("derived IB interface name %q contains invalid characters", ifaceName)
	}

	mtu := ib.MTU
	if mtu == 0 {
		if strings.EqualFold(ib.IPoIBMode, "connected") {
			mtu = 65520
		} else {
			mtu = 2044 // datagram mode default
		}
	}

	mode := strings.ToLower(ib.IPoIBMode)
	if mode == "" {
		mode = "datagram"
	}

	var sb strings.Builder
	sb.WriteString("[connection]\n")
	fmt.Fprintf(&sb, "id=%s\n", ifaceName)
	sb.WriteString("type=infiniband\n")
	fmt.Fprintf(&sb, "interface-name=%s\n", ifaceName)
	sb.WriteString("\n")

	sb.WriteString("[infiniband]\n")
	fmt.Fprintf(&sb, "transport-mode=%s\n", mode)
	fmt.Fprintf(&sb, "mtu=%d\n", mtu)
	if len(ib.PKeys) > 0 {
		// Write the first partition key; additional pkeys require separate profiles.
		fmt.Fprintf(&sb, "p-key=%s\n", ib.PKeys[0])
	}
	sb.WriteString("\n")

	sb.WriteString("[ipv4]\n")
	if ib.IPAddress != "" {
		ip, prefix := parseIPCIDR(ib.IPAddress)
		sb.WriteString("method=manual\n")
		fmt.Fprintf(&sb, "address1=%s/%s\n", ip, prefix)
	} else {
		sb.WriteString("method=disabled\n")
	}
	sb.WriteString("\n")

	sb.WriteString("[ipv6]\n")
	sb.WriteString("method=ignore\n")

	filename := filepath.Join(nmDir, ifaceName+".nmconnection")
	return os.WriteFile(filename, []byte(sb.String()), 0o600)
}

// ibDeviceToIPoIBName maps an IB device name to its IPoIB interface name.
// e.g. "mlx5_0" → "ib0", "mlx5_1" → "ib1", "hfi1_0" → "ib0"
func ibDeviceToIPoIBName(devName string) string {
	// Extract trailing digit after the last underscore for the IB port index.
	idx := strings.LastIndex(devName, "_")
	if idx >= 0 && idx < len(devName)-1 {
		suffix := devName[idx+1:]
		// Only use the numeric suffix as the ib interface index.
		allDigits := true
		for _, c := range suffix {
			if c < '0' || c > '9' {
				allDigits = false
				break
			}
		}
		if allDigits {
			return "ib" + suffix
		}
	}
	return "ib0"
}

// writeHostname writes /etc/hostname and updates /etc/hosts.
func writeHostname(mountRoot, hostname, fqdn string) error {
	if hostname == "" {
		return nil
	}

	hostnameFile := filepath.Join(mountRoot, "etc", "hostname")
	if err := os.MkdirAll(filepath.Dir(hostnameFile), 0o755); err != nil {
		return err
	}
	if err := os.WriteFile(hostnameFile, []byte(hostname+"\n"), 0o644); err != nil {
		return fmt.Errorf("write /etc/hostname: %w", err)
	}

	// Update /etc/hosts: add or replace the 127.0.1.1 line.
	hostsFile := filepath.Join(mountRoot, "etc", "hosts")
	existing, err := os.ReadFile(hostsFile)
	if err != nil && !os.IsNotExist(err) {
		return fmt.Errorf("read /etc/hosts: %w", err)
	}

	// Build the new hosts content.
	var lines []string
	for _, line := range strings.Split(string(existing), "\n") {
		// Remove any existing 127.0.1.1 lines.
		if strings.HasPrefix(strings.TrimSpace(line), "127.0.1.1") {
			continue
		}
		lines = append(lines, line)
	}

	entry := "127.0.1.1\t" + hostname
	if fqdn != "" && fqdn != hostname {
		entry = "127.0.1.1\t" + fqdn + "\t" + hostname
	}
	lines = append(lines, entry)

	content := strings.Join(lines, "\n")
	if !strings.HasSuffix(content, "\n") {
		content += "\n"
	}
	return os.WriteFile(hostsFile, []byte(content), 0o644)
}

const (
	clusterHostsBegin = "# --- clustr cluster hosts (auto-generated) ---"
	clusterHostsEnd   = "# --- end clustr cluster hosts ---"
)

// writeClusterHosts writes a clearly delimited block of cluster host entries
// into the deployed filesystem's /etc/hosts. The block is idempotent: if a
// clustr cluster block already exists (from a previous deploy), it is replaced.
// Standard localhost entries are preserved; only the cluster block is managed.
func writeClusterHosts(mountRoot string, hosts []api.HostEntry) error {
	hostsFile := filepath.Join(mountRoot, "etc", "hosts")
	existing, err := os.ReadFile(hostsFile)
	if err != nil && !os.IsNotExist(err) {
		return fmt.Errorf("read /etc/hosts: %w", err)
	}

	// Rebuild the file: keep all lines that are NOT inside an existing cluster block,
	// then append the new cluster block at the end.
	var kept []string
	inBlock := false
	for _, line := range strings.Split(string(existing), "\n") {
		if line == clusterHostsBegin {
			inBlock = true
			continue
		}
		if line == clusterHostsEnd {
			inBlock = false
			continue
		}
		if !inBlock {
			kept = append(kept, line)
		}
	}

	// Strip trailing blank lines before appending the new block, then ensure
	// exactly one blank line separates the existing content from the block.
	for len(kept) > 0 && strings.TrimSpace(kept[len(kept)-1]) == "" {
		kept = kept[:len(kept)-1]
	}

	var sb strings.Builder
	if len(kept) > 0 {
		sb.WriteString(strings.Join(kept, "\n"))
		sb.WriteString("\n\n")
	}

	sb.WriteString(clusterHostsBegin)
	sb.WriteString("\n")
	for _, h := range hosts {
		var line string
		if h.FQDN != "" && h.FQDN != h.Hostname {
			line = fmt.Sprintf("%-15s %s %s", h.IP, h.FQDN, h.Hostname)
		} else {
			line = fmt.Sprintf("%-15s %s", h.IP, h.Hostname)
		}
		if len(h.Aliases) > 0 {
			line += " " + strings.Join(h.Aliases, " ")
		}
		sb.WriteString(line + "\n")
	}
	sb.WriteString(clusterHostsEnd)
	sb.WriteString("\n")

	return os.WriteFile(hostsFile, []byte(sb.String()), 0o644)
}

// writeNetworkConfig writes NetworkManager keyfile connections for each interface.
// This is the standard config format on Rocky Linux 8/9/10, RHEL 8/9/10, and derivatives.
//
// When interfaces is empty a single fallback "wired-dhcp" connection is written
// that matches any wired interface (no interface-name restriction) and requests a
// DHCP lease. This ensures the node is reachable after first boot even when the
// caller does not specify explicit interface config. Without this, Rocky Linux 10
// will not configure any NIC by default because NM's no-auto-default behavior
// is distribution-controlled and may not activate wired devices automatically.
func writeNetworkConfig(mountRoot string, interfaces []api.InterfaceConfig) error {
	nmDir := filepath.Join(mountRoot, "etc", "NetworkManager", "system-connections")
	if err := os.MkdirAll(nmDir, 0o700); err != nil {
		return fmt.Errorf("mkdir NM connections: %w", err)
	}

	if len(interfaces) == 0 {
		// Write a wildcard DHCP profile so the first wired interface comes up.
		// Inject DNS so the node can resolve external hostnames (e.g. dnf repos)
		// without falling back to the QEMU NAT default (10.0.2.3) which is
		// unreachable from the provisioning network.
		dnsServers := clusterDNSServers()
		dnsLine := fmt.Sprintf("dns=%s;\n", strings.Join(dnsServers, ";"))
		fallback := "[connection]\n" +
			"id=wired-dhcp\n" +
			"type=ethernet\n" +
			"autoconnect=true\n" +
			"autoconnect-priority=-100\n" +
			"\n" +
			"[ethernet]\n" +
			"\n" +
			"[ipv4]\n" +
			"method=auto\n" +
			dnsLine +
			"\n" +
			"[ipv6]\n" +
			"method=ignore\n"
		path := filepath.Join(nmDir, "wired-dhcp.nmconnection")
		return os.WriteFile(path, []byte(fallback), 0o600)
	}

	for _, iface := range interfaces {
		if err := writeNMKeyfile(nmDir, iface); err != nil {
			return fmt.Errorf("interface %s: %w", iface.Name, err)
		}
	}
	return nil
}

// clusterDNSServers returns the DNS resolver list for deployed nodes.
//
// Source priority:
//  1. CLUSTR_DNS_SERVERS env var (comma-separated IPs, e.g. "10.99.0.1,1.1.1.1")
//  2. Hard default: "1.1.1.1,8.8.8.8"
//
// The QEMU NAT default (10.0.2.3) is intentionally NOT used here — it is
// unreachable from the provisioning network (10.99.0.0/24) and causes every
// outbound network call (dnf, systemd-resolved, etc.) on the deployed node to
// time out silently.  Operators running a local DNS forwarder (e.g. dnsmasq on
// 10.99.0.1) should set CLUSTR_DNS_SERVERS=10.99.0.1.
func clusterDNSServers() []string {
	raw := os.Getenv("CLUSTR_DNS_SERVERS")
	if raw == "" {
		return []string{"1.1.1.1", "8.8.8.8"}
	}
	var servers []string
	for _, s := range strings.Split(raw, ",") {
		s = strings.TrimSpace(s)
		if s != "" {
			servers = append(servers, s)
		}
	}
	if len(servers) == 0 {
		return []string{"1.1.1.1", "8.8.8.8"}
	}
	return servers
}

// writeClustrDHCPProfile writes a minimal NetworkManager connection profile that
// auto-connects via DHCP on the first available ethernet interface. This ensures
// clustr-verify-boot.service can reach the server after reboot even when all other
// NM profiles have static IPs or are bound to a specific interface name.
//
// File permissions MUST be 0600 — NetworkManager silently ignores world-readable
// connection files. The high autoconnect-priority (100) means this profile is
// preferred over NM's built-in defaults but yields to operator-created profiles
// (which should specify interface-name= to pin to a specific NIC).
//
// DNS servers from clusterDNSServers() are injected so the node can resolve
// external hostnames (e.g. for dnf package installs) immediately after boot.
func writeClustrDHCPProfile(mountRoot string) error {
	nmDir := filepath.Join(mountRoot, "etc", "NetworkManager", "system-connections")
	if err := os.MkdirAll(nmDir, 0o755); err != nil {
		return fmt.Errorf("mkdir NM connections: %w", err)
	}

	dnsServers := clusterDNSServers()
	// NM keyfile dns= uses semicolon-separated IPs with a trailing semicolon.
	dnsLine := fmt.Sprintf("dns=%s;\n", strings.Join(dnsServers, ";"))

	profile := "[connection]\n" +
		"id=clustr-dhcp\n" +
		"type=ethernet\n" +
		"autoconnect=true\n" +
		"autoconnect-priority=100\n" +
		"\n" +
		"[ipv4]\n" +
		"method=auto\n" +
		dnsLine +
		"\n" +
		"[ipv6]\n" +
		"method=auto\n"

	profilePath := filepath.Join(nmDir, "clustr-dhcp.nmconnection")
	return os.WriteFile(profilePath, []byte(profile), 0o600)
}

// writeNMKeyfile writes a single NetworkManager keyfile for an interface.
func writeNMKeyfile(nmDir string, iface api.InterfaceConfig) error {
	name := iface.Name
	if name == "" {
		name = iface.MACAddress
	}

	// Validate the interface name before using it as a filename component.
	if !ifaceNameRe.MatchString(name) {
		return fmt.Errorf("interface name %q contains invalid characters (must match ^[a-zA-Z0-9._:-]+$)", name)
	}

	// Parse IP and prefix from CIDR.
	ip, prefix := parseIPCIDR(iface.IPAddress)

	var sb strings.Builder
	sb.WriteString("[connection]\n")
	fmt.Fprintf(&sb, "id=%s\n", name)
	fmt.Fprintf(&sb, "type=ethernet\n")
	fmt.Fprintf(&sb, "interface-name=%s\n", name)
	sb.WriteString("\n")

	sb.WriteString("[ethernet]\n")
	if iface.MACAddress != "" {
		fmt.Fprintf(&sb, "mac-address=%s\n", iface.MACAddress)
	}
	if iface.MTU > 0 {
		fmt.Fprintf(&sb, "mtu=%d\n", iface.MTU)
	}
	sb.WriteString("\n")

	sb.WriteString("[ipv4]\n")
	// DNS: prefer per-interface override; fall back to cluster-wide servers so
	// the node can reach external repos (e.g. dnf) without QEMU NAT DNS (10.0.2.3)
	// which is unreachable from the provisioning network (10.99.0.0/24).
	dnsServers := iface.DNS
	if len(dnsServers) == 0 {
		dnsServers = clusterDNSServers()
	}
	if ip != "" {
		sb.WriteString("method=manual\n")
		fmt.Fprintf(&sb, "address1=%s/%s", ip, prefix)
		if iface.Gateway != "" {
			fmt.Fprintf(&sb, ",%s", iface.Gateway)
		}
		sb.WriteString("\n")
		fmt.Fprintf(&sb, "dns=%s;\n", strings.Join(dnsServers, ";"))
	} else {
		sb.WriteString("method=auto\n")
		fmt.Fprintf(&sb, "dns=%s;\n", strings.Join(dnsServers, ";"))
	}
	sb.WriteString("\n")

	sb.WriteString("[ipv6]\n")
	sb.WriteString("method=ignore\n")

	filename := filepath.Join(nmDir, name+".nmconnection")
	// Mode 0600 is required by NM — it refuses to load world-readable keyfiles.
	return os.WriteFile(filename, []byte(sb.String()), 0o600)
}

// parseIPCIDR splits "192.168.1.10/24" into ("192.168.1.10", "24").
// Returns ("", "") if the input is empty or malformed.
// When a bare IP is provided without a CIDR prefix, a warning is logged and
// /24 is used as the default — callers should always supply an explicit prefix.
func parseIPCIDR(cidr string) (ip, prefix string) {
	parts := strings.SplitN(cidr, "/", 2)
	if len(parts) == 2 {
		return parts[0], parts[1]
	}
	if len(parts) == 1 && parts[0] != "" {
		logger().Warn().Str("ip", parts[0]).
			Msg("IP address has no CIDR prefix, defaulting to /24")
		return parts[0], "24"
	}
	return "", ""
}

// writeSSHKeys writes authorized_keys for root.
// Each key is validated with golang.org/x/crypto/ssh before writing;
// invalid keys are skipped with a warning log so one malformed entry
// does not silently block legitimate access.
func writeSSHKeys(mountRoot string, keys []string) error {
	log := logger()
	sshDir := filepath.Join(mountRoot, "root", ".ssh")
	if err := os.MkdirAll(sshDir, 0o700); err != nil {
		return fmt.Errorf("mkdir .ssh: %w", err)
	}

	var validKeys []string
	for _, key := range keys {
		trimmed := strings.TrimSpace(key)
		if trimmed == "" {
			continue
		}
		if _, _, _, _, err := gossh.ParseAuthorizedKey([]byte(trimmed)); err != nil {
			log.Warn().Str("key_prefix", truncateKey(trimmed)).
				Err(err).Msg("finalize: skipping invalid SSH public key")
			continue
		}
		validKeys = append(validKeys, trimmed)
	}

	authKeysPath := filepath.Join(sshDir, "authorized_keys")
	content := strings.Join(validKeys, "\n")
	if len(validKeys) > 0 && !strings.HasSuffix(content, "\n") {
		content += "\n"
	}

	return os.WriteFile(authKeysPath, []byte(content), 0o600)
}

// ensureShadowFile writes /etc/shadow inside the deployed chroot if it is absent
// or empty. Some base images omit /etc/shadow because the image build runs as
// non-root and cannot preserve the 640/root:shadow ownership.
//
// Without /etc/shadow, pam_unix.so account phase fails for every user because
// /etc/passwd shows "x" in the password field (indicating shadow passwords are
// in use), and pam_unix.so returns PAM_AUTH_ERR when it cannot open the shadow
// file — even for public-key SSH logins.
//
// We generate a shadow entry for every user in /etc/passwd, using "!!" (locked
// password) as the hash. A locked password prevents password-based login but
// does NOT block SSH public-key authentication. The last-change field is set to
// today's day-count (days since 1970-01-01) so no forced-change logic triggers.
//
// Ownership and permissions are set to root:root 0o000 (same as RHEL default of
// 0o000 when shadow-utils manages the file). We cannot set root:shadow because
// the deploy context may not have the shadow GID available; 0o000 is the RHEL
// default and pam_unix.so opens the file as root.
func ensureShadowFile(mountRoot string) error {
	log := logger()
	shadowPath := filepath.Join(mountRoot, "etc", "shadow")
	passwdPath := filepath.Join(mountRoot, "etc", "passwd")

	// If shadow already exists and is non-empty, leave it alone.
	if info, err := os.Stat(shadowPath); err == nil && info.Size() > 0 {
		log.Info().Msg("finalize: /etc/shadow already present — skipping generation")
		return nil
	}

	// Read /etc/passwd to enumerate users that need shadow entries.
	passwdBytes, err := os.ReadFile(passwdPath)
	if err != nil {
		return fmt.Errorf("ensureShadowFile: read /etc/passwd: %w", err)
	}

	// Day count since 1970-01-01 (the lastchg field).
	daysSinceEpoch := int(time.Now().UTC().Unix() / 86400)

	var lines []string
	for _, line := range strings.Split(string(passwdBytes), "\n") {
		line = strings.TrimSpace(line)
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}
		fields := strings.Split(line, ":")
		if len(fields) < 7 {
			continue
		}
		username := fields[0]
		// shadow format: name:pwd:lastchg:min:max:warn:inactive:expire:reserved
		// "!!" = locked password (no password-based login, pubkey still works).
		// min=0, max=99999, warn=7, inactive empty, expire empty.
		shadow := fmt.Sprintf("%s:!!:%d:0:99999:7:::", username, daysSinceEpoch)
		lines = append(lines, shadow)
	}

	content := strings.Join(lines, "\n") + "\n"
	if err := os.WriteFile(shadowPath, []byte(content), 0o000); err != nil {
		return fmt.Errorf("ensureShadowFile: write /etc/shadow: %w", err)
	}

	log.Info().Int("entries", len(lines)).Msg("finalize: /etc/shadow written (was absent from base image — locked passwords, pubkey auth unaffected)")
	return nil
}

// truncateKey returns the first 40 characters of a key string for log messages.
func truncateKey(key string) string {
	if len(key) <= 40 {
		return key
	}
	return key[:40] + "..."
}

// applyKernelArgs appends extra kernel args to the GRUB default config.
// It patches GRUB_CMDLINE_LINUX and GRUB_CMDLINE_LINUX_DEFAULT when present.
// If both exist, both are patched. If neither exists, GRUB_CMDLINE_LINUX is
// appended so the args take effect regardless of which variable grub2-mkconfig
// uses on the target distro.
func applyKernelArgs(ctx context.Context, mountRoot, kernelArgs string) error {
	grubDefault := filepath.Join(mountRoot, "etc", "default", "grub")
	raw, err := os.ReadFile(grubDefault)
	if err != nil {
		if os.IsNotExist(err) {
			return nil // no GRUB config — might be a systemd-boot system
		}
		return fmt.Errorf("read grub config: %w", err)
	}

	content := string(raw)
	updatedLinux := false
	updatedDefault := false
	var newLines []string
	for _, line := range strings.Split(content, "\n") {
		switch {
		case strings.HasPrefix(line, "GRUB_CMDLINE_LINUX="):
			// Strip trailing quote, append args, re-add quote.
			trimmed := strings.TrimSuffix(strings.TrimSpace(line), `"`)
			line = trimmed + " " + kernelArgs + `"`
			updatedLinux = true
		case strings.HasPrefix(line, "GRUB_CMDLINE_LINUX_DEFAULT="):
			trimmed := strings.TrimSuffix(strings.TrimSpace(line), `"`)
			line = trimmed + " " + kernelArgs + `"`
			updatedDefault = true
		}
		newLines = append(newLines, line)
	}

	if !updatedLinux && !updatedDefault {
		// Neither key present — append GRUB_CMDLINE_LINUX as the canonical fallback.
		newLines = append(newLines, fmt.Sprintf(`GRUB_CMDLINE_LINUX="%s"`, kernelArgs))
	}

	newContent := strings.Join(newLines, "\n")
	if err := os.WriteFile(grubDefault, []byte(newContent), 0o644); err != nil {
		return fmt.Errorf("write grub config: %w", err)
	}

	// Note: under the content-only model (ADR-0009), applyBootConfig generates
	// the grub.cfg and BLS entries from scratch AFTER applyNodeConfig runs.
	// The /etc/default/grub edit above is the durable per-node override;
	// applyBootConfig will incorporate it when generating BLS entries (S2-04).
	// We do NOT run grub2-mkconfig here — it would be overwritten immediately
	// and may fail on RAID layouts (diskfilter write restriction).
	return nil
}

// findGrubCfg looks for grub.cfg in common locations within the mount root.
func findGrubCfg(mountRoot string) string {
	candidates := []string{
		"/boot/grub2/grub.cfg",
		"/boot/grub/grub.cfg",
		"/boot/efi/EFI/rocky/grub.cfg",
		"/boot/efi/EFI/redhat/grub.cfg",
		"/boot/efi/EFI/centos/grub.cfg",
	}
	for _, c := range candidates {
		if _, err := os.Stat(filepath.Join(mountRoot, c)); err == nil {
			return c // return path relative to chroot root
		}
	}
	return ""
}

// installKernelInChroot installs the kernel and GRUB2 bootloader packages inside
// the deployed chroot using dnf. This is needed when the image was captured
// without /boot contents (e.g. --one-file-system capture with a separate /boot
// partition on the source). After this function returns, /boot/grub2/grub.cfg
// and at least one vmlinuz + initramfs pair will exist in the chroot.
//
// The function mounts /proc, /sys, and /dev into the chroot before running dnf
// because dnf's scriptlets require these virtual filesystems.
func installKernelInChroot(ctx context.Context, mountRoot, targetDisk string) error {
	log := logger()

	// Bind-mount virtual filesystems required by dnf/rpm scriptlets.
	type bindMount struct{ src, dst string }
	binds := []bindMount{
		{"/proc", filepath.Join(mountRoot, "proc")},
		{"/sys", filepath.Join(mountRoot, "sys")},
		{"/dev", filepath.Join(mountRoot, "dev")},
		{"/dev/pts", filepath.Join(mountRoot, "dev", "pts")},
	}
	for _, b := range binds {
		if err := os.MkdirAll(b.dst, 0o755); err != nil {
			return fmt.Errorf("mkdir %s: %w", b.dst, err)
		}
		if out, err := exec.CommandContext(ctx, "mount", "--bind", b.src, b.dst).CombinedOutput(); err != nil {
			return fmt.Errorf("bind-mount %s → %s: %w\n%s", b.src, b.dst, err, string(out))
		}
	}
	defer func() {
		// Unmount in reverse order.
		for i := len(binds) - 1; i >= 0; i-- {
			_ = exec.Command("umount", "-l", binds[i].dst).Run()
		}
	}()

	// Ensure /dev/fd exists inside the chroot — dracut and other tools use
	// bash process substitution which requires /dev/fd → /proc/self/fd.
	devFD := filepath.Join(mountRoot, "dev", "fd")
	if _, err := os.Lstat(devFD); os.IsNotExist(err) {
		_ = os.Symlink("/proc/self/fd", devFD)
	}

	// Copy the host's /etc/resolv.conf into the chroot so dnf can reach the network.
	resolvSrc := "/etc/resolv.conf"
	resolvDst := filepath.Join(mountRoot, "etc", "resolv.conf")
	if data, err := os.ReadFile(resolvSrc); err == nil {
		_ = os.WriteFile(resolvDst, data, 0o644)
	}

	// Ensure /tmp exists and is writable inside the chroot.
	// dnf requires /tmp for its download cache and RPM scriptlets.
	// Images captured without /tmp (or with sticky-bit /tmp permissions stripped)
	// will cause dnf to fail with "mkstemp: No such file or directory".
	tmpDir := filepath.Join(mountRoot, "tmp")
	if err := os.MkdirAll(tmpDir, 0o1777); err != nil {
		log.Warn().Err(err).Str("path", tmpDir).Msg("finalize/boot: could not create /tmp in chroot (non-fatal)")
	}
	// Ensure /boot/grub2 directory exists so grub2-mkconfig can write grub.cfg.
	grub2Dir := filepath.Join(mountRoot, "boot", "grub2")
	if err := os.MkdirAll(grub2Dir, 0o755); err != nil {
		log.Warn().Err(err).Str("path", grub2Dir).Msg("finalize/boot: could not create /boot/grub2 in chroot (non-fatal)")
	}

	// Install kernel and grub2-pc inside the deployed chroot.
	// We always use "chroot <mountRoot> dnf" because the initramfs (deployment host)
	// does not have dnf — it must be invoked from within the deployed image, which
	// is a full Rocky Linux 9 system with /usr/bin/dnf present.
	//
	// IMPORTANT: disable all third-party repos (epel, epel-next, etc.) during
	// this install. The initramfs environment has no external DNS resolution and
	// any attempt to reach mirrors.fedoraproject.org or similar will time out and
	// cause dnf to fail. Kernel and grub2 are in the base Rocky repos (baseos,
	// appstream, extras) which are served by the local mirrorlist / fastest-mirror
	// cache inside the chroot, but to be safe we explicitly enable only baseos and
	// appstream and disable everything else.
	packages := []string{"kernel", "grub2-pc", "grub2-tools"}
	log.Info().Strs("packages", packages).Msg("finalize/boot: installing kernel + grub2 via chroot dnf (baseos+appstream only)")
	chrootDnfArgs := append([]string{
		mountRoot, "dnf", "-y",
		"--setopt=install_weak_deps=False",
		"--disablerepo=*",
		"--enablerepo=baseos",
		"--enablerepo=appstream",
		"install",
	}, packages...)
	if err := runAndLog(ctx, "chroot-dnf-kernel-install", exec.CommandContext(ctx, "chroot", chrootDnfArgs...)); err != nil {
		return fmt.Errorf("chroot dnf install kernel+grub2: %w", err)
	}

	// Install GRUB to the target disk MBR now that the grub2-pc package is present.
	// grub2-install needs access to /boot (already mounted) and /dev.
	bootDir := filepath.Join(mountRoot, "boot")
	grubInstallArgs := []string{"--target=i386-pc", "--boot-directory=" + bootDir, "--recheck", targetDisk}
	log.Info().Str("disk", targetDisk).Msg("finalize/boot: re-running grub2-install after kernel install")
	if err := runAndLog(ctx, "grub2-install", exec.CommandContext(ctx, "grub2-install", grubInstallArgs...)); err != nil {
		log.Warn().Err(err).Msg("finalize/boot: grub2-install after kernel install failed (non-fatal)")
	}

	// Generate grub.cfg inside the chroot.
	grubCfgInChroot := "/boot/grub2/grub.cfg"
	log.Info().Str("grub_cfg", grubCfgInChroot).Msg("finalize/boot: generating grub.cfg via chroot grub2-mkconfig")
	mkcfgCmd := exec.CommandContext(ctx, "chroot", mountRoot, "grub2-mkconfig", "-o", grubCfgInChroot)
	if err := runAndLog(ctx, "grub2-mkconfig", mkcfgCmd); err != nil {
		return fmt.Errorf("finalize/boot: grub2-mkconfig after kernel install: %w", err)
	}
	log.Info().Msg("finalize/boot: kernel + grub2 installed and configured")
	return nil
}

// runGrub2InstallEFIInChroot runs `grub2-install --target=x86_64-efi` inside the
// deployed chroot. Running inside the chroot is critical for UEFI installs:
//
//   - grub2-install reads GRUB modules from /usr/lib/grub/x86_64-efi/ in the
//     chroot, not from the deploy initramfs. This guarantees module version
//     consistency with the deployed OS's grub2 package. A mismatch causes GRUB
//     to crash at boot with "symbol not found" or "invalid magic number", which
//     manifests as a flash/flicker at the OVMF boot picker and immediate return
//     to firmware.
//   - grub2-install writes modules to /boot/grub2/x86_64-efi/ inside the chroot
//     (= <mountRoot>/boot/grub2/x86_64-efi/ on the host). These must match the
//     grubx64.efi binary — same build, same version.
//
// Flags:
//   - --target=x86_64-efi : EFI install (does NOT need a disk argument, unlike i386-pc)
//   - --efi-directory=/boot/efi : path to the mounted ESP (chroot-relative)
//   - --boot-directory=/boot : path to /boot partition (chroot-relative)
//   - --bootloader-id=rocky : writes to EFI/rocky/ subdir on the ESP
//   - --removable : writes \EFI\BOOT\BOOTX64.EFI with the correct prefix baked in;
//     this is the post-deploy boot target for UEFI removable-media auto-discovery
//     (UEFI §3.5.1.1 — works across reimages, NVRAM wipes, AC loss)
//   - --no-nvram : do NOT write NVRAM entries — clustr relies on removable-media
//     auto-discovery; no custom OS NVRAM entry is created (see docs/boot-architecture.md §8)
//
// The function sets up the bind mounts that grub2-install requires (/proc for
// device detection, /sys for efivars read, /dev for block device access).
func runGrub2InstallEFIInChroot(ctx context.Context, mountRoot string) error {
	log := logger()

	// Bind-mount virtual filesystems required by grub2-install inside the chroot.
	// /proc: grub2-install reads /proc/devices and /proc/mounts for device detection.
	// /sys:  grub2-install checks /sys/firmware/efi for EFI subsystem presence.
	// /dev:  grub2-install opens block devices directly (e.g. /dev/sda, /dev/nvme0n1).
	// /dev/pts: some versions of grub2-install open a pts for console I/O.
	type bindMount struct{ src, dst string }
	binds := []bindMount{
		{"/proc", filepath.Join(mountRoot, "proc")},
		{"/sys", filepath.Join(mountRoot, "sys")},
		{"/dev", filepath.Join(mountRoot, "dev")},
		{"/dev/pts", filepath.Join(mountRoot, "dev", "pts")},
	}
	for _, b := range binds {
		if err := os.MkdirAll(b.dst, 0o755); err != nil {
			return fmt.Errorf("mkdir %s: %w", b.dst, err)
		}
		if out, err := exec.CommandContext(ctx, "mount", "--bind", b.src, b.dst).CombinedOutput(); err != nil {
			return fmt.Errorf("bind-mount %s → %s: %w\n%s", b.src, b.dst, err, string(out))
		}
	}
	defer func() {
		for i := len(binds) - 1; i >= 0; i-- {
			_ = exec.Command("umount", "-l", binds[i].dst).Run()
		}
	}()

	// Ensure /dev/fd exists inside the chroot — dracut and other tools use
	// bash process substitution which requires /dev/fd → /proc/self/fd.
	devFD := filepath.Join(mountRoot, "dev", "fd")
	if _, err := os.Lstat(devFD); os.IsNotExist(err) {
		_ = os.Symlink("/proc/self/fd", devFD)
	}

	// Inside the chroot, /boot/efi is the ESP (already mounted by mountPartitions)
	// and /boot is the boot partition. Paths are chroot-relative.
	args := []string{
		mountRoot,
		"grub2-install",
		"--target=x86_64-efi",
		"--efi-directory=/boot/efi",
		"--boot-directory=/boot",
		"--bootloader-id=rocky",
		"--removable",
		"--no-nvram",
		"--recheck",
		"--force",
	}
	log.Info().Strs("chroot_args", args[1:]).Msg("finalize/boot: running grub2-install --target=x86_64-efi inside chroot")
	if err := runAndLog(ctx, "grub2-install-efi-chroot", exec.CommandContext(ctx, "chroot", args...)); err != nil {
		return fmt.Errorf("chroot grub2-install --target=x86_64-efi: %w", err)
	}
	log.Info().Msg("finalize/boot: grub2-install --target=x86_64-efi inside chroot succeeded")
	return nil
}

// applyBootConfig generates all topology-specific boot state from scratch
// (ADR-0009 content-only model). It must be called after the rootfs tarball has
// been extracted and after grub2-install has written the bootloader MBR/bios-boot.
//
// New flow (generate, do not patch):
//
//  1. Generate a fresh machine-id and write to /etc/machine-id +
//     /var/lib/dbus/machine-id.
//  2. Write /etc/fstab.d/00-clustr-os.fstab with OS-disk mounts, assemble
//     /etc/fstab from all fstab.d files in lexicographic order (§5A split).
//  3. Generate BLS entries from scratch under /boot/loader/entries/ using the
//     new machine-id and target root UUID.
//  4. Prepare the RAID dracut config (if RAID layout).
//  5. Run dracut --regenerate-all --no-hostonly --force inside a chroot to
//     rebuild initramfs with the correct modules for the target hardware.
//  6. Run kernel-install add for each production kernel so a BLS entry exists
//     even when Anaconda's %post did not call kernel-install during image build.
//  7. Pin GRUB saved_entry to the production kernel (prevents rescue-first boot).
//  8. Write a minimal hand-crafted grub.cfg (no grub2-mkconfig, no save_env,
//     no submenus — clean bootable config for provisioned compute nodes).
//  9. Scrub SSH host keys so sshd regenerates unique keys on first boot.
//
// All UUID-patch and sed-based code has been deleted. Nothing is patched;
// everything topology-specific is generated fresh from the target partition UUIDs.
//
// partDevs must be in the same order as layout.Partitions (index i → device i+1).
func applyBootConfig(ctx context.Context, mountRoot, targetDisk string, layout api.DiskLayout, partDevs []string, progress ProgressMessenger) error {
	log := logger()

	// reportStep sends a human-readable sub-step description to the UI and console.
	// The zerolog server journal already has detail; this surfaces it to the operator.
	reportStep := func(msg string) {
		log.Info().Msg("finalize/boot: " + msg)
		if progress != nil {
			progress.SetMessage(msg)
		}
	}

	// ── 1. Generate fresh machine-id ────────────────────────────────────────
	// Content-only images carry an empty /etc/machine-id placeholder.
	// Generate a 128-bit random hex string and write it now so that BLS entry
	// filenames (which embed the machine-id) are deterministic within this deploy.
	reportStep("Generating machine identity")
	log.Info().Msg("  → Generating machine ID...")
	machineID, err := generateMachineID()
	if err != nil {
		return fmt.Errorf("finalize/boot: generate machine-id: %w", err)
	}
	machineIDPath := filepath.Join(mountRoot, "etc", "machine-id")
	if err := os.WriteFile(machineIDPath, []byte(machineID+"\n"), 0o644); err != nil {
		return fmt.Errorf("finalize/boot: write machine-id: %w", err)
	}
	dbusMachineIDPath := filepath.Join(mountRoot, "var", "lib", "dbus", "machine-id")
	if err := os.MkdirAll(filepath.Dir(dbusMachineIDPath), 0o755); err == nil {
		_ = os.WriteFile(dbusMachineIDPath, []byte(machineID+"\n"), 0o444)
	}
	log.Info().Str("machine_id", machineID).Msg("finalize/boot: fresh machine-id generated and written")

	// ── 2. Generate fstab (§5A ownership split) ──────────────────────────────
	// Write OS-disk mounts to /etc/fstab.d/00-clustr-os.fstab, then assemble
	// /etc/fstab from all fstab.d files in lexicographic order.
	// Paths matching /etc/fstab.d/9*-*.fstab are owned by the image overlay and
	// are never overwritten here.
	reportStep("Writing filesystem table")
	log.Info().Msg("  → Writing fstab...")
	log.Info().Msg("finalize/boot: generating /etc/fstab.d/00-clustr-os.fstab")
	if err := writeFstabD(ctx, mountRoot, layout, partDevs); err != nil {
		return fmt.Errorf("finalize/boot: fstab generation: %w", err)
	}
	log.Info().Msg("finalize/boot: /etc/fstab assembled from fstab.d")

	// Get the root UUID now — needed for BLS entries and grub.cfg.
	reportStep("Resolving partition UUIDs")
	log.Info().Msg("  → Resolving partition UUIDs...")
	var rootUUID, bootUUID string
	for i, p := range layout.Partitions {
		if i >= len(partDevs) {
			break
		}
		u, uErr := getUUID(ctx, partDevs[i])
		if uErr != nil {
			log.Warn().Err(uErr).Str("device", partDevs[i]).Str("mp", p.MountPoint).
				Msg("finalize/boot: could not get UUID for partition")
			continue
		}
		switch p.MountPoint {
		case "/":
			rootUUID = u
		case "/boot":
			bootUUID = u
		}
	}
	if rootUUID == "" {
		return fmt.Errorf("finalize/boot: could not determine root partition UUID — deploy cannot continue")
	}
	// When /boot is not a separate partition, grub reads from root.
	if bootUUID == "" {
		bootUUID = rootUUID
	}
	log.Info().Str("root_uuid", rootUUID).Str("boot_uuid", bootUUID).
		Msg("finalize/boot: partition UUIDs resolved")

	// ── 3. Populate /boot with kernels from /usr/lib/modules/ ───────────────
	// Rocky 10+ uses kernel-install(8) which places kernels at
	// /usr/lib/modules/<kver>/vmlinuz rather than /boot/vmlinuz-<kver>.
	// Content-only images have an empty /boot/, so BLS generation and grub.cfg
	// both fail with "no kernel found". Copy any missing kernels into /boot now
	// so that all subsequent steps (BLS, dracut, grub.cfg) can find them.
	reportStep("Copying kernel to boot partition")
	log.Info().Msg("  → Copying kernel to /boot...")
	modulesDir := filepath.Join(mountRoot, "usr", "lib", "modules")
	if modEntries, modErr := os.ReadDir(modulesDir); modErr == nil {
		for _, e := range modEntries {
			if !e.IsDir() {
				continue
			}
			kver := e.Name()
			src := filepath.Join(modulesDir, kver, "vmlinuz")
			dst := filepath.Join(mountRoot, "boot", "vmlinuz-"+kver)
			if _, statErr := os.Stat(src); statErr != nil {
				// No vmlinuz under this modules dir — skip.
				continue
			}
			if _, statErr := os.Stat(dst); statErr == nil {
				// Already present in /boot — nothing to do.
				continue
			}
			data, readErr := os.ReadFile(src)
			if readErr != nil {
				log.Warn().Err(readErr).Str("src", src).
					Msg("finalize/boot: could not read kernel from /usr/lib/modules (non-fatal)")
				continue
			}
			if writeErr := os.WriteFile(dst, data, 0o644); writeErr != nil {
				log.Warn().Err(writeErr).Str("dst", dst).
					Msg("finalize/boot: could not copy kernel to /boot (non-fatal)")
				continue
			}
			log.Info().Str("kver", kver).Str("src", src).Str("dst", dst).
				Msg("finalize/boot: copied vmlinuz from /usr/lib/modules to /boot")
		}
	}

	// ── 4. Discover kernels and generate BLS entries ─────────────────────────
	// List all vmlinuz-* files under /boot to enumerate installed kernels.
	// One BLS entry is generated per kernel with the fresh machine-id.
	log.Info().Msg("finalize/boot: generating BLS boot entries")
	if err := generateBLSEntries(mountRoot, machineID, rootUUID, layout); err != nil {
		// Non-fatal: if BLS fails the hand-crafted grub.cfg below still boots
		// the first kernel it finds directly. Log loudly so the operator knows.
		log.Warn().Err(err).Msg("WARNING finalize/boot: BLS entry generation failed — node will boot via direct grub.cfg menuentry")
	}

	// ── 5. RAID dracut prep (if RAID layout) ────────────────────────────────
	// Inject mdraid dracut module config, mdadm binary, and udev rules into the
	// chroot so dracut builds an initramfs that can assemble md arrays on first boot.
	if len(layout.RAIDArrays) > 0 {
		if err := prepareDracutForRAID(mountRoot, layout); err != nil {
			log.Warn().Err(err).Msg("finalize/boot: RAID dracut prep failed (non-fatal)")
		}
	}

	// Ensure the standard virtual filesystem mount points exist in the deployed
	// root before calling dracut. These directories are NOT present in tar
	// archives captured via rsync (rsync skips empty stub directories for virtual
	// mounts like /proc, /sys, /dev). Without them, bind-mounts fail with ENOENT.
	for _, dir := range []string{"proc", "sys", "dev", "dev/pts", "run", "var/tmp"} {
		target := filepath.Join(mountRoot, dir)
		if err := os.MkdirAll(target, 0o755); err != nil {
			log.Warn().Err(err).Str("dir", target).Msg("finalize/boot: could not create chroot mount dir (non-fatal)")
		}
	}

	// ── 6. Bind-mount virtual filesystems and rebuild initramfs via dracut ───
	// dracut's module-setup.sh scripts inspect /proc/cpuinfo, /sys/module/*,
	// and /dev/fd to detect hardware and build the correct initramfs.
	// Without these bind-mounts, dracut warns "/proc/ is not mounted" and may
	// omit critical modules (XFS, virtio_scsi, etc.) from the initramfs.
	type bindMount struct{ src, dst string }
	dracutBinds := []bindMount{
		{"/proc", filepath.Join(mountRoot, "proc")},
		{"/sys", filepath.Join(mountRoot, "sys")},
		{"/dev", filepath.Join(mountRoot, "dev")},
		{"/dev/pts", filepath.Join(mountRoot, "dev", "pts")},
	}
	var mountedPaths []string
	for _, b := range dracutBinds {
		if out, mountErr := exec.CommandContext(ctx, "mount", "--bind", b.src, b.dst).CombinedOutput(); mountErr != nil {
			log.Warn().Err(mountErr).Str("src", b.src).Str("dst", b.dst).
				Str("output", string(out)).Msg("finalize/boot: bind-mount failed — dracut may omit hardware modules (non-fatal)")
		} else {
			mountedPaths = append(mountedPaths, b.dst)
		}
	}
	defer func() {
		for i := len(mountedPaths) - 1; i >= 0; i-- {
			_ = exec.Command("umount", "-l", mountedPaths[i]).Run()
		}
	}()

	// Ensure /dev/fd exists inside the chroot — dracut and other tools use
	// bash process substitution which requires /dev/fd → /proc/self/fd.
	devFD := filepath.Join(mountRoot, "dev", "fd")
	if _, err := os.Lstat(devFD); os.IsNotExist(err) {
		_ = os.Symlink("/proc/self/fd", devFD)
	}

	// Rocky 10+ dracut with BLS Type 2 layout writes kernel+initramfs into
	// /boot/efi/<machine-id>/<kernel-version>/. Create the directory tree
	// so dracut doesn't fail with "Directory does not exist".
	espMachineDir := filepath.Join(mountRoot, "boot", "efi", machineID)
	if err := os.MkdirAll(espMachineDir, 0o755); err != nil {
		log.Warn().Err(err).Str("path", espMachineDir).
			Msg("finalize/boot: could not create ESP machine-id dir (non-fatal — dracut may fail)")
	}
	if modEntries, readErr := os.ReadDir(filepath.Join(mountRoot, "lib", "modules")); readErr == nil {
		for _, e := range modEntries {
			if e.IsDir() && e.Name() != "" {
				_ = os.MkdirAll(filepath.Join(espMachineDir, e.Name()), 0o755)
			}
		}
	}

	// --no-hostonly: build a generic initramfs (hardware-agnostic, not tailored
	// to the deploy host's physical devices). Required because we are running in
	// a PXE initramfs environment with different hardware than the target node.
	// --force: overwrite any existing initramfs images without prompting.
	// --regenerate-all: rebuild for every installed kernel version.
	reportStep("Rebuilding initramfs")
	log.Info().Msg("  → Rebuilding initramfs with dracut...")
	log.Info().Msg("finalize/boot: rebuilding initramfs via dracut --no-hostonly --regenerate-all")
	dracutCmd := exec.CommandContext(ctx, "chroot", mountRoot,
		"dracut", "--force", "--no-hostonly", "--regenerate-all")
	if err := runAndLog(ctx, "dracut", dracutCmd); err != nil {
		log.Warn().Err(err).
			Msg("WARNING finalize/boot: dracut --regenerate-all failed — initramfs may lack hardware drivers for target; node may not boot on different hardware")
	} else {
		log.Info().Msg("finalize/boot: dracut complete")
	}

	// ── 7. Delete all BLS entries — hand-crafted grub.cfg is sole boot authority ─
	// Option A: clustr writes grub.cfg, clustr controls entry order, production
	// kernel is always set default=0. Rocky's GRUB2 has BLS auto-scan compiled
	// in and cannot be disabled without rebuilding GRUB. Without nuking all BLS
	// entries, rescue entries (sorted "0-rescue-" prefix) appear before the
	// production kernel in the GRUB menu, causing RAID1 nodes to boot rescue
	// (which lacks mdraid modules) and hang at "Probing EDD".
	//
	// Nuke them all. The hand-crafted grub.cfg written in step 7 is the sole
	// boot authority. No kernel-install is needed — grub.cfg references the
	// kernel by path directly.
	if allBLSEntries, globErr := filepath.Glob(filepath.Join(mountRoot, "boot/loader/entries/*.conf")); globErr == nil {
		for _, e := range allBLSEntries {
			if removeErr := os.Remove(e); removeErr != nil {
				log.Warn().Err(removeErr).Str("path", e).Msg("finalize/boot: could not remove BLS entry (non-fatal)")
			} else {
				log.Info().Str("path", e).Msg("finalize/boot: removed BLS entry")
			}
		}
		log.Info().Int("count", len(allBLSEntries)).Msg("finalize/boot: all BLS entries deleted — grub.cfg is sole boot authority")
	}

	// ── 8. Write minimal hand-crafted grub.cfg ──────────────────────────────
	// Do NOT run grub2-mkconfig: it produces save_env / saved_entry calls that
	// fail on BIOS RAID (diskfilter writes are not supported) and injects
	// unnecessary multi-boot / rescue complexity. We generate a minimal config
	// directly from the discovered kernels and target UUIDs.
	reportStep("Writing bootloader configuration")
	log.Info().Msg("  → Writing grub.cfg...")
	log.Info().Msg("finalize/boot: writing minimal hand-crafted grub.cfg")
	if err := writeHandcraftedGrubCfg(mountRoot, rootUUID, bootUUID, layout); err != nil {
		// Non-fatal: the node can still attempt to boot via BLS entries.
		log.Warn().Err(err).Msg("WARNING finalize/boot: could not write grub.cfg (non-fatal if BLS is active)")
	} else {
		log.Info().Msg("finalize/boot: grub.cfg written")
	}

	// ── 9. SSH host key scrub ────────────────────────────────────────────────
	// Remove host keys baked into the image so sshd regenerates unique keys
	// on first boot via the ssh-keygen firstboot unit.
	reportStep("Scrubbing SSH host keys")
	log.Info().Msg("  → Scrubbing SSH host keys...")
	hostKeys, _ := filepath.Glob(filepath.Join(mountRoot, "etc", "ssh", "ssh_host_*"))
	for _, k := range hostKeys {
		if err := os.Remove(k); err != nil {
			log.Warn().Err(err).Str("key", k).
				Msg("finalize/boot: could not remove SSH host key (non-fatal)")
		}
	}
	if len(hostKeys) > 0 {
		log.Info().Int("count", len(hostKeys)).
			Msg("finalize/boot: SSH host keys removed — sshd will regenerate on first boot")
	}

	return nil
}

// generateMachineID returns a fresh 128-bit random hex string suitable for
// /etc/machine-id (32 lowercase hex chars, no hyphens, no newline).
func generateMachineID() (string, error) {
	b := make([]byte, 16)
	if _, err := rand.Read(b); err != nil {
		return "", fmt.Errorf("read random bytes: %w", err)
	}
	return hex.EncodeToString(b), nil
}

// writeFstabD writes /etc/fstab.d/00-clustr-os.fstab with OS-disk mounts only
// (root, /boot, swap, /boot/efi), then assembles /etc/fstab by concatenating
// all fstab.d files in lexicographic order (ADR-0009 §5A ownership split).
//
// Files matching /etc/fstab.d/9*-*.fstab are owned by the image overlay and
// are NEVER touched here. /etc/fstab itself is always regenerated.
func writeFstabD(ctx context.Context, mountRoot string, layout api.DiskLayout, partDevs []string) error {
	log := logger()

	fstabDDir := filepath.Join(mountRoot, "etc", "fstab.d")
	if err := os.MkdirAll(fstabDDir, 0o755); err != nil {
		return fmt.Errorf("mkdir fstab.d: %w", err)
	}

	var sb strings.Builder
	sb.WriteString("# /etc/fstab.d/00-clustr-os.fstab — OS-disk mounts generated by clustr\n")
	sb.WriteString("# DO NOT EDIT: regenerated on every reimage\n")
	sb.WriteString("# <device>  <mountpoint>  <fstype>  <options>  <dump>  <pass>\n\n")

	for i, p := range layout.Partitions {
		if p.MountPoint == "" {
			continue
		}
		switch p.Filesystem {
		case "", "biosboot", "bios_grub":
			continue
		}
		if i >= len(partDevs) {
			log.Warn().Int("partition", i+1).Msg("writeFstabD: partDevs slice shorter than layout — skipping")
			continue
		}

		uuid, err := getUUID(ctx, partDevs[i])
		if err != nil {
			return fmt.Errorf("partition %d (%s): %w", i+1, partDevs[i], err)
		}

		opts := fstabMountOpts(p.Filesystem, p.MountPoint)

		dump := 0
		pass := 0
		switch p.MountPoint {
		case "/":
			dump, pass = 1, 1
		case "/boot":
			dump, pass = 1, 2
		}

		if p.Filesystem == "swap" {
			fmt.Fprintf(&sb, "UUID=%-36s  %-12s  %-6s  %-20s  %d  %d\n",
				uuid, "swap", "swap", "defaults", 0, 0)
		} else {
			fmt.Fprintf(&sb, "UUID=%-36s  %-12s  %-6s  %-20s  %d  %d\n",
				uuid, p.MountPoint, p.Filesystem, opts, dump, pass)
		}
		log.Info().Str("uuid", uuid).Str("mountpoint", p.MountPoint).
			Str("device", partDevs[i]).Msg("finalize/boot: fstab.d entry written")
	}

	osFstabPath := filepath.Join(fstabDDir, "00-clustr-os.fstab")
	if err := os.WriteFile(osFstabPath, []byte(sb.String()), 0o644); err != nil {
		return fmt.Errorf("write 00-clustr-os.fstab: %w", err)
	}

	// Assemble /etc/fstab from all fstab.d files in lexicographic order.
	// Files matching 9*-*.fstab are image-overlay-owned — include them but NEVER overwrite.
	entries, err := os.ReadDir(fstabDDir)
	if err != nil {
		return fmt.Errorf("read fstab.d: %w", err)
	}

	var assembled strings.Builder
	assembled.WriteString("# /etc/fstab — assembled by clustr from /etc/fstab.d/*.fstab\n\n")
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".fstab") {
			continue
		}
		data, readErr := os.ReadFile(filepath.Join(fstabDDir, e.Name()))
		if readErr != nil {
			log.Warn().Err(readErr).Str("file", e.Name()).Msg("finalize/boot: could not read fstab.d fragment (skipping)")
			continue
		}
		assembled.Write(data)
		assembled.WriteString("\n")
	}

	fstabPath := filepath.Join(mountRoot, "etc", "fstab")
	if err := os.WriteFile(fstabPath, []byte(assembled.String()), 0o644); err != nil {
		return fmt.Errorf("write /etc/fstab: %w", err)
	}
	return nil
}

// generateBLSEntries creates a Boot Loader Specification entry for each kernel
// found under /boot/vmlinuz-* in the deployed rootfs. Each entry is written to
// /boot/loader/entries/<machineID>-<kver>.conf with the target root UUID on the
// options line and no save_env / submenus.
func generateBLSEntries(mountRoot, machineID, rootUUID string, layout api.DiskLayout) error {
	log := logger()

	bootDir := filepath.Join(mountRoot, "boot")
	entries, err := os.ReadDir(bootDir)
	if err != nil {
		return fmt.Errorf("read /boot: %w", err)
	}

	blsDir := filepath.Join(mountRoot, "boot", "loader", "entries")
	if err := os.MkdirAll(blsDir, 0o755); err != nil {
		return fmt.Errorf("mkdir BLS entries dir: %w", err)
	}

	// Build base kernel cmdline: console + RAID UUIDs if applicable.
	baseOpts := []string{"ro", "console=ttyS0,115200"}
	if len(layout.RAIDArrays) > 0 {
		baseOpts = append(baseOpts, "rd.auto")
		for _, spec := range layout.RAIDArrays {
			mdDev := "/dev/" + spec.Name
			ctx := context.Background()
			uuid, uuidErr := getRaidArrayUUID(ctx, mdDev)
			if uuidErr != nil {
				log.Warn().Err(uuidErr).Str("device", mdDev).
					Msg("finalize/boot: could not get RAID array UUID — rd.md.uuid will be missing from BLS entry")
			} else if uuid != "" {
				baseOpts = append(baseOpts, "rd.md.uuid="+uuid)
			}
		}
	}

	// Safety net: if /boot/vmlinuz-* is still empty, fall back to kernels
	// found at /usr/lib/modules/<kver>/vmlinuz (Rocky 10 kernel-install layout).
	hasBootKernel := false
	for _, e := range entries {
		if strings.HasPrefix(e.Name(), "vmlinuz-") {
			hasBootKernel = true
			break
		}
	}
	if !hasBootKernel {
		log.Warn().Msg("finalize/boot: no vmlinuz-* in /boot — falling back to /usr/lib/modules/*/vmlinuz for BLS generation")
		modulesDir := filepath.Join(mountRoot, "usr", "lib", "modules")
		if modEntries, modErr := os.ReadDir(modulesDir); modErr == nil {
			for _, e := range modEntries {
				if !e.IsDir() {
					continue
				}
				kver := e.Name()
				src := filepath.Join(modulesDir, kver, "vmlinuz")
				dst := filepath.Join(bootDir, "vmlinuz-"+kver)
				if _, statErr := os.Stat(src); statErr != nil {
					continue
				}
				data, readErr := os.ReadFile(src)
				if readErr != nil {
					continue
				}
				if writeErr := os.WriteFile(dst, data, 0o644); writeErr != nil {
					log.Warn().Err(writeErr).Str("dst", dst).Msg("finalize/boot: BLS fallback: could not copy vmlinuz to /boot")
					continue
				}
				log.Info().Str("kver", kver).Msg("finalize/boot: BLS fallback: copied vmlinuz from /usr/lib/modules to /boot")
			}
		}
		// Re-read /boot after fallback copies.
		if refreshed, reErr := os.ReadDir(bootDir); reErr == nil {
			entries = refreshed
		}
	}

	written := 0
	for _, e := range entries {
		name := e.Name()
		if !strings.HasPrefix(name, "vmlinuz-") {
			continue
		}
		kver := strings.TrimPrefix(name, "vmlinuz-")
		if kver == "" {
			continue
		}

		// Check for a matching initramfs.
		initramfsName := "initramfs-" + kver + ".img"
		initramfsPath := filepath.Join(bootDir, initramfsName)
		if _, statErr := os.Stat(initramfsPath); statErr != nil {
			// dracut may not have run yet or may have named it differently.
			// Write the BLS entry anyway with the expected initramfs path;
			// dracut will create it when it runs (step 5 in applyBootConfig).
			log.Warn().Str("kver", kver).Str("expected", initramfsName).
				Msg("finalize/boot: initramfs not found — BLS entry will reference it anyway (dracut will create it)")
		}

		opts := append([]string{"root=UUID=" + rootUUID}, baseOpts...)
		optLine := strings.Join(opts, " ")

		blsContent := fmt.Sprintf(`title Rocky Linux (%s)
version %s
linux /vmlinuz-%s
initrd /initramfs-%s.img
options %s
`, kver, kver, kver, kver, optLine)

		blsPath := filepath.Join(blsDir, machineID+"-"+kver+".conf")
		if err := os.WriteFile(blsPath, []byte(blsContent), 0o644); err != nil {
			log.Warn().Err(err).Str("path", blsPath).Msg("finalize/boot: could not write BLS entry")
			continue
		}
		log.Info().Str("kver", kver).Str("root_uuid", rootUUID).
			Msg("finalize/boot: BLS entry generated")
		written++
	}

	if written == 0 {
		return fmt.Errorf("no kernels found under /boot/vmlinuz-* — cannot generate BLS entries")
	}
	log.Info().Int("count", written).Msg("finalize/boot: BLS entries generated")
	return nil
}

// writeHandcraftedGrubCfg writes a minimal grub.cfg that boots the first
// available kernel without save_env, saved_entry, load_env, or submenus.
// This config is safe on BIOS RAID (diskfilter read-only) and UEFI alike.
//
// On UEFI+BLS systems the BLS entries take precedence; this grub.cfg acts as
// a fallback for non-BLS-capable grub builds.
func writeHandcraftedGrubCfg(mountRoot, rootUUID, bootUUID string, layout api.DiskLayout) error {
	log := logger()

	bootDir := filepath.Join(mountRoot, "boot")
	entries, err := os.ReadDir(bootDir)
	if err != nil {
		return fmt.Errorf("read /boot: %w", err)
	}

	// Find the first kernel.
	var kver string
	for _, e := range entries {
		if strings.HasPrefix(e.Name(), "vmlinuz-") {
			kver = strings.TrimPrefix(e.Name(), "vmlinuz-")
			break
		}
	}
	// Safety net: fall back to /usr/lib/modules/<kver>/vmlinuz if /boot is empty.
	if kver == "" {
		log.Warn().Msg("finalize/boot: no vmlinuz-* in /boot — falling back to /usr/lib/modules/*/vmlinuz for grub.cfg")
		modulesDir := filepath.Join(mountRoot, "usr", "lib", "modules")
		if modEntries, modErr := os.ReadDir(modulesDir); modErr == nil {
			for _, e := range modEntries {
				if !e.IsDir() {
					continue
				}
				candidate := e.Name()
				src := filepath.Join(modulesDir, candidate, "vmlinuz")
				dst := filepath.Join(bootDir, "vmlinuz-"+candidate)
				if _, statErr := os.Stat(src); statErr != nil {
					continue
				}
				data, readErr := os.ReadFile(src)
				if readErr != nil {
					continue
				}
				if writeErr := os.WriteFile(dst, data, 0o644); writeErr != nil {
					log.Warn().Err(writeErr).Str("dst", dst).Msg("finalize/boot: grub.cfg fallback: could not copy vmlinuz to /boot")
					continue
				}
				log.Info().Str("kver", candidate).Msg("finalize/boot: grub.cfg fallback: copied vmlinuz from /usr/lib/modules to /boot")
				kver = candidate
				break
			}
		}
	}
	if kver == "" {
		return fmt.Errorf("no kernel found under /boot/vmlinuz-* or /usr/lib/modules/*/vmlinuz — cannot write grub.cfg")
	}

	// Build kernel cmdline.
	cmdlineOpts := []string{"root=UUID=" + rootUUID, "ro", "console=ttyS0,115200"}
	if len(layout.RAIDArrays) > 0 {
		cmdlineOpts = append(cmdlineOpts, "rd.auto")
		for _, spec := range layout.RAIDArrays {
			mdDev := "/dev/" + spec.Name
			ctx := context.Background()
			uuid, uuidErr := getRaidArrayUUID(ctx, mdDev)
			if uuidErr != nil {
				log.Warn().Err(uuidErr).Str("device", mdDev).
					Msg("finalize/boot: grub.cfg: could not get RAID array UUID — rd.md.uuid will be missing")
			} else if uuid != "" {
				cmdlineOpts = append(cmdlineOpts, "rd.md.uuid="+uuid)
			}
		}
	}
	cmdline := strings.Join(cmdlineOpts, " ")

	// Determine if the /boot filesystem module is needed.
	// Default to ext2 for grub (covers both xfs and ext4 — grub uses ext2 module for ext4 too).
	grubBootFSMod := "ext2"
	for _, p := range layout.Partitions {
		if p.MountPoint == "/" || p.MountPoint == "/boot" {
			switch p.Filesystem {
			case "xfs":
				grubBootFSMod = "xfs"
			}
		}
	}

	var cfg strings.Builder
	cfg.WriteString("# /boot/grub2/grub.cfg — generated by clustr (content-only deploy)\n")
	cfg.WriteString("# DO NOT EDIT: regenerated on every reimage\n\n")
	// set default=0: the hand-crafted menuentry below is always entry 0 and is
	// the sole boot entry. All BLS entries are deleted before this file is written
	// so Rocky's built-in BLS auto-scan finds nothing. No load_env, no saved_entry,
	// no rescue menuentry — clean deterministic boot for provisioned compute nodes.
	cfg.WriteString("set default=0\n")
	cfg.WriteString("set timeout=5\n")
	cfg.WriteString("insmod gzio\n")
	cfg.WriteString("insmod part_gpt\n")
	fmt.Fprintf(&cfg, "insmod %s\n", grubBootFSMod)
	// RAID layouts need diskfilter module to read /boot from the md array.
	if len(layout.RAIDArrays) > 0 {
		cfg.WriteString("insmod diskfilter\n")
		cfg.WriteString("insmod mdraid1x\n")
	}
	fmt.Fprintf(&cfg, "search --no-floppy --fs-uuid --set=root %s\n\n", bootUUID)

	// Kernel path relative to GRUB root:
	// - When /boot is a separate partition (bootUUID != rootUUID), GRUB's root IS
	//   the /boot partition, so kernel is at /vmlinuz-<kver> (not /boot/vmlinuz-).
	// - When /boot is on the root partition (bootUUID == rootUUID), GRUB's root IS
	//   the root partition, so kernel is at /boot/vmlinuz-<kver>.
	kernelPrefix := "/boot"
	if bootUUID != rootUUID {
		kernelPrefix = ""
	}
	fmt.Fprintf(&cfg, "menuentry \"Rocky Linux (%s)\" {\n", kver)
	fmt.Fprintf(&cfg, "  linux %s/vmlinuz-%s %s\n", kernelPrefix, kver, cmdline)
	fmt.Fprintf(&cfg, "  initrd %s/initramfs-%s.img\n", kernelPrefix, kver)
	cfg.WriteString("}\n")

	grubCfgPath := filepath.Join(mountRoot, "boot", "grub2", "grub.cfg")
	if err := os.MkdirAll(filepath.Dir(grubCfgPath), 0o755); err != nil {
		return fmt.Errorf("mkdir grub2 dir: %w", err)
	}
	if err := os.WriteFile(grubCfgPath, []byte(cfg.String()), 0o644); err != nil {
		return fmt.Errorf("write grub.cfg: %w", err)
	}
	log.Info().Str("kver", kver).Str("root_uuid", rootUUID).
		Msg("finalize/boot: hand-crafted grub.cfg written")
	return nil
}

// prepareDracutForRAID injects the mdraid dracut configuration, mdadm binary,
// and md udev rules into the deployed chroot so dracut can build an initramfs
// that assembles md RAID arrays before mounting root on first boot.
func prepareDracutForRAID(mountRoot string, layout api.DiskLayout) error {
	log := logger()

	dracutConfDir := filepath.Join(mountRoot, "etc", "dracut.conf.d")
	if err := os.MkdirAll(dracutConfDir, 0o755); err != nil {
		return fmt.Errorf("mkdir dracut.conf.d: %w", err)
	}

	dracutRAIDConf := filepath.Join(dracutConfDir, "10-clustr-mdraid.conf")
	confContent := `# Written by clustr during deployment for RAID layouts.
# Forces mdraid dracut module to be included in the initramfs so the OS can
# assemble its md RAID arrays before mounting root on first boot.
add_dracutmodules+=" mdraid "
# mdadmconf=yes tells dracut to copy /etc/mdadm.conf into the initramfs so
# mdadm can assemble arrays by UUID without requiring rd.md.uuid= cmdline args.
mdadmconf=yes
`
	if err := os.WriteFile(dracutRAIDConf, []byte(confContent), 0o644); err != nil {
		return fmt.Errorf("write mdraid dracut config: %w", err)
	}
	log.Info().Str("path", dracutRAIDConf).
		Msg("finalize/boot: wrote mdraid dracut config")

	// Inject mdadm binary if absent.
	chrootMdadm := filepath.Join(mountRoot, "usr", "sbin", "mdadm")
	if _, err := os.Stat(chrootMdadm); os.IsNotExist(err) {
		hostMdadm, lookErr := exec.LookPath("mdadm")
		if lookErr != nil {
			log.Warn().Msg("finalize/boot: mdadm not found on deploy host — dracut mdraid module may fail")
		} else {
			if copyErr := copyFile(hostMdadm, chrootMdadm, 0o755); copyErr != nil {
				log.Warn().Err(copyErr).Msg("finalize/boot: could not inject mdadm into chroot")
			} else {
				log.Info().Str("dst", chrootMdadm).Msg("finalize/boot: injected mdadm into chroot")
			}
		}
	}

	// Inject mdadm udev rules.
	chrootUdevRulesDir := filepath.Join(mountRoot, "usr", "lib", "udev", "rules.d")
	if err := os.MkdirAll(chrootUdevRulesDir, 0o755); err != nil {
		return fmt.Errorf("mkdir udev rules.d: %w", err)
	}
	for _, ruleSpec := range []struct {
		name    string
		content string
	}{
		{"63-md-raid-arrays.rules", mdRaidArraysRules},
		{"64-md-raid-assembly.rules", mdRaidAssemblyRules},
	} {
		chrootRule := filepath.Join(chrootUdevRulesDir, ruleSpec.name)
		if _, statErr := os.Stat(chrootRule); statErr == nil {
			log.Info().Str("file", ruleSpec.name).
				Msg("finalize/boot: mdadm udev rule already present — skipping injection")
			continue
		}
		if err := os.WriteFile(chrootRule, []byte(ruleSpec.content), 0o644); err != nil {
			log.Warn().Err(err).Str("dst", chrootRule).
				Msg("finalize/boot: could not write mdadm udev rule (non-fatal)")
		} else {
			log.Info().Str("file", ruleSpec.name).
				Msg("finalize/boot: wrote mdadm udev rule into chroot")
		}
	}
	return nil
}


// getUUID returns the filesystem UUID of a block device using blkid.
// Returns an error if blkid is unavailable or the device has no UUID (e.g.
// unformatted or a bios_grub partition).
func getUUID(ctx context.Context, device string) (string, error) {
	out, err := exec.CommandContext(ctx, "blkid", "-s", "UUID", "-o", "value", device).Output()
	if err != nil {
		return "", fmt.Errorf("blkid %s: %w", device, err)
	}
	uuid := strings.TrimSpace(string(out))
	if uuid == "" {
		return "", fmt.Errorf("blkid %s: no UUID (partition may be unformatted)", device)
	}
	return uuid, nil
}

