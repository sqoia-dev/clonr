// ldapDomainFromBaseDN extracts the first DC component from a base DN.
// "dc=cluster,dc=local" → "cluster"
func ldapDomainFromBaseDN(baseDN string) string {
	parts := strings.SplitN(baseDN, ",", 2)
	if len(parts) > 0 {
		p := strings.TrimSpace(parts[0])
		lower := strings.ToLower(p)
		if strings.HasPrefix(lower, "dc=") {
			return lower[3:]
		}
	}
	return "cluster"
}

// SlurmInstallAuditFn is a callback invoked by installSlurmInChroot when the
// dnf install step fails. The caller (writeSlurmConfig → applyNodeConfig) wires
// this to the server audit service so operators can query the audit log for
// failure details without grepping server logs.
//
// action is always db.AuditActionSlurmInstallFailed ("slurm.install.failed").
// detail contains the last 2 KB of combined dnf stdout+stderr.
type SlurmInstallAuditFn func(ctx context.Context, nodeID, action, detail string)

// writeSudoersDropin writes a sudoers drop-in file to
// <mountRoot>/etc/sudoers.d/<group_cn> granting sudo access to the named LDAP group.
// The file is written with mode 0440 as required by sudo.
// installSlurmInChroot adds a dnf repo and installs Slurm + munge packages
// inside the chroot rooted at mountRoot.  All steps are non-fatal — on failure
// the deploy continues and the GAP-14 binary-existence guard in the service-
// enable block will simply skip enabling the missing daemons.
//
// Package selection:
//   - controller / dbd node (hasSlurmdbd): slurm slurm-slurmctld munge
//   - compute node (hasGres): slurm slurm-slurmd munge
//   - fallback (no role yet): slurm munge  (minimal; enables munge at least)
//
// EL9/EL10 mismatch detection: reads /etc/os-release from the chroot to
// determine the deployed OS version. If the URL contains "EL_9", "EL_10", or
// the clustr /repo/el9-x86_64/ pattern, and the image's version doesn't match,
// the mismatch is logged (non-fatal) so the operator can correct the repo URL.
//
// gpgKeyBytes, when non-nil, is the clustr GPG release signing public key
// (ASCII-armored). It is written to the chroot at
// /etc/pki/rpm-gpg/RPM-GPG-KEY-clustr for forward-compatibility but is NOT
// used for chroot dnf gpgcheck (see GAP-17 comment in Step 3a).  gpgcheck=0
// is always used because the bundle contains third-party RPMs (Rocky BaseOS,
// EPEL) that are signed with keys not present in the chroot; bundle integrity
// is enforced via SHA256 verification at download time instead.
func installSlurmInChroot(ctx context.Context, mountRoot, nodeID, repoURL string, hasSlurmdbd, hasGres bool, auditFn SlurmInstallAuditFn, gpgKeyBytes []byte) {
	log := logger()

	// EL9 / EL10 mismatch detection — read VERSION_ID from the chroot's os-release.
	// This runs before dnf so the operator sees the mismatch warning early in the log.
	osReleasePath := filepath.Join(mountRoot, "etc", "os-release")
	if raw, err := os.ReadFile(osReleasePath); err == nil {
		imageEL := extractELVersion(string(raw))
		urlEL := elVersionFromURL(repoURL)
		if imageEL != "" && urlEL != "" && imageEL != urlEL {
			log.Warn().
				Str("repo_url", repoURL).
				Str("url_el_version", urlEL).
				Str("image_el_version", imageEL).
				Str("node_id", nodeID).
				Msgf("finalize slurm: auto-install: EL version mismatch — repo is for EL%s but image is EL%s. "+
					"Update slurm_repo_url via POST /api/v1/modules/slurm/enable to use the correct EL%s repo.",
					urlEL, imageEL, imageEL)
		}
	}

	// Build package list for this role.
	pkgs := []string{"slurm", "munge"}
	if hasSlurmdbd {
		pkgs = append(pkgs, "slurm-slurmctld")
	}
	if hasGres {
		pkgs = append(pkgs, "slurm-slurmd")
	}

	log.Info().Str("repo_url", repoURL).Strs("packages", pkgs).
		Msg("finalize slurm: auto-install: adding repo and installing packages in chroot")

	// Step 1: Bind-mount virtual filesystems required by dnf/rpm scriptlets.
	// Without /proc, /sys, and /dev, rpm post-install scripts (ldconfig, systemctl
	// preset, etc.) fail with "No such file or directory" or exit non-zero.
	// The munge and slurm packages both run scriptlets that require these mounts.
	// Same pattern used by installKernelInChroot.
	type bindMount struct{ src, dst string }
	binds := []bindMount{
		{"/proc", filepath.Join(mountRoot, "proc")},
		{"/sys", filepath.Join(mountRoot, "sys")},
		{"/dev", filepath.Join(mountRoot, "dev")},
		{"/dev/pts", filepath.Join(mountRoot, "dev", "pts")},
	}
	for _, b := range binds {
		if err := os.MkdirAll(b.dst, 0o755); err != nil {
			log.Warn().Err(err).Str("path", b.dst).
				Msg("finalize slurm: auto-install: could not mkdir bind-mount dst (non-fatal)")
		}
		if out, err := exec.CommandContext(ctx, "mount", "--bind", b.src, b.dst).CombinedOutput(); err != nil {
			log.Warn().Err(err).Str("src", b.src).Str("dst", b.dst).Str("output", string(out)).
				Msg("finalize slurm: auto-install: bind-mount failed (non-fatal, dnf scriptlets may fail)")
		}
	}
	defer func() {
		// Unmount bind-mounts in reverse order so /dev/pts is unmounted before /dev.
		for i := len(binds) - 1; i >= 0; i-- {
			_ = exec.Command("umount", "-l", binds[i].dst).Run()
		}
	}()

	// Step 2: DNS prep — copy the deploy environment's /etc/resolv.conf into
	// the chroot and ensure /etc/hosts has localhost entries.
	//
	// The clustr-server IP is the DNS resolver for the provisioning network
	// (pxe/dhcp.go advertises it via DHCP option 6). The initramfs udhcpc
	// writes the received nameserver into /etc/resolv.conf, so the deploy
	// environment has a working resolver that points at the clustr-server.
	// Copying that file into the chroot gives dnf the same DNS path.
	//
	// The chroot's own resolv.conf (from the captured base image) may be empty
	// or stale (e.g. 10.0.2.3 from QEMU NAT). Overwriting it ensures dnf uses
	// the correct nameserver during the deploy finalize phase.
	//
	// The file is overwritten; on first boot NetworkManager writes its own
	// resolv.conf based on the NM connection profiles injected by applyNodeConfig.
	//
	// Even though the current primary repo URL is an IP address (clustr-server),
	// we always prepare DNS because dnf may fetch EPEL or other deps that require
	// name resolution, and future code paths will depend on it.
	resolvSrc := "/etc/resolv.conf"
	resolvDst := filepath.Join(mountRoot, "etc", "resolv.conf")
	if data, err := os.ReadFile(resolvSrc); err == nil {
		if err := os.WriteFile(resolvDst, data, 0o644); err != nil {
			log.Warn().Err(err).Str("dst", resolvDst).
				Msg("finalize slurm: auto-install: could not write resolv.conf into chroot (non-fatal, DNS may fail)")
		} else {
			log.Info().Msg("finalize slurm: auto-install: copied /etc/resolv.conf into chroot for dnf DNS resolution")
		}
	} else {
		log.Warn().Err(err).Msg("finalize slurm: auto-install: could not read host /etc/resolv.conf (non-fatal)")
	}

	// Ensure /etc/hosts has at minimum the localhost entries. A base image
	// captured from a QEMU install VM may have an empty or minimal hosts file.
	// Without localhost entries, rpm scriptlets that do getent lookups fail.
	hostsPath := filepath.Join(mountRoot, "etc", "hosts")
	const localhostHosts = "127.0.0.1 localhost localhost.localdomain\n::1 localhost localhost.localdomain\n"
	if raw, err := os.ReadFile(hostsPath); err != nil || !strings.Contains(string(raw), "localhost") {
		if werr := os.WriteFile(hostsPath, []byte(localhostHosts), 0o644); werr != nil {
			log.Warn().Err(werr).Msg("finalize slurm: auto-install: could not write /etc/hosts into chroot (non-fatal)")
		} else {
			log.Info().Msg("finalize slurm: auto-install: wrote localhost entries to /etc/hosts in chroot")
		}
	}

	// Step 2.5: Strip any pre-existing OpenHPC Slurm packages from the chroot
	// before installing clustr's Slurm RPMs. Base images built before the
	// OpenHPC-removal change (PR4) may contain slurm-ohpc* packages that own
	// the same files as clustr-slurm RPMs (/usr/sbin/slurmctld, etc.), causing
	// dnf install to fail with file conflicts.
	//
	// This is a defense-in-depth step: the canonical fix is rebuilding the base
	// image without OpenHPC packages (see docs/imagebuilder.md). This strip
	// makes old images work without requiring an immediate image rebuild.
	//
	// || true makes this idempotent — fine if the packages are not present.
	ohpcStripArgs := []string{
		mountRoot, "dnf", "-y", "--setopt=tsflags=noscripts",
		"--disablerepo=*", "remove",
		"slurm-ohpc", "slurm-ohpc-*", "ohpc-slurm-*",
	}
	ohpcOut, ohpcErr := exec.CommandContext(ctx, "chroot", ohpcStripArgs...).CombinedOutput()
	if ohpcErr != nil {
		// dnf remove exits non-zero when no packages match the glob — that is the
		// normal case for images already built without OpenHPC. Log at debug level.
		log.Debug().Str("dnf_output", string(ohpcOut)).
			Msg("finalize slurm: auto-install: OpenHPC strip — no packages removed (image is already clean)")
	} else if strings.Contains(string(ohpcOut), "Removed") {
		log.Info().Str("dnf_output", string(ohpcOut)).
			Msg("finalize slurm: auto-install: stripped pre-existing OpenHPC slurm packages from image (defense-in-depth)")
	}

	// Step 3a: Write all three GPG public keys into the chroot.
	//
	// GAP-17 hardening (clustr5): the bundle now ships two repo subdirectories:
	//   - el9-x86_64/      — clustr-built RPMs (signed with clustr key)
	//   - el9-x86_64-deps/ — passthrough Rocky/EPEL RPMs (signed with their keys)
	//
	// All three keys are written to /etc/pki/rpm-gpg/ so that dnf can perform
	// per-RPM gpgcheck=1 against the correct key for each repo stanza.
	// The gpgKeyBytes parameter carries the caller-provided clustr key (for
	// backward compat with custom-URL callers); the rocky and EPEL keys are
	// taken from the embedded binaries directly (server.RockyKeyBytes, server.EPELKeyBytes).
	//
	// For the clustr-builtin repo URL path (where gpgKeyBytes is non-nil), all
	// three keys are written. For operator-provided custom URLs, only the clustr
	// key is written (if provided); the two-stanza repo is not emitted for custom
	// URLs since we don't know whether they follow the clustr split-bundle layout.
	gpgKeyDir := filepath.Join(mountRoot, "etc", "pki", "rpm-gpg")
	// isClusterBuiltin: true when gpgKeyBytes is provided (the clustr-builtin repo URL
	// path). False for operator-provided custom repo URLs (where gpgKeyBytes is nil).
	isClusterBuiltin := len(gpgKeyBytes) > 0

	if isClusterBuiltin {
		// Write all three embedded GPG keys: clustr, rocky-9, EPEL-9.
		// server.WriteAllGPGKeysToRepo reads from the embedded bytes (//go:embed)
		// not from disk, so this works even in the deploy initramfs where the
		// build/slurm/keys/ directory is not present.
		if err := os.MkdirAll(gpgKeyDir, 0o755); err != nil {
			log.Warn().Err(err).Msg("finalize slurm: auto-install: could not create /etc/pki/rpm-gpg in chroot (non-fatal)")
		} else if err := server.WriteAllGPGKeysToRepo(gpgKeyDir); err != nil {
			log.Warn().Err(err).Msg("finalize slurm: auto-install: could not write GPG keys into chroot (non-fatal)")
		} else {
			log.Info().Msg("finalize slurm: auto-install: wrote RPM-GPG-KEY-{clustr,rocky-9,EPEL-9} into chroot /etc/pki/rpm-gpg/")
		}
	}

	// Step 3b: Write the .repo file(s) directly rather than calling dnf config-manager,
	// because config-manager may not be installed in the chroot image.
	//
	// For clustr-builtin repo URLs (gpgKeyBytes non-nil), emit a two-stanza repo:
	//   [clustr-slurm]      → el9-x86_64/ (clustr-built), gpgcheck=1 vs clustr key
	//   [clustr-slurm-deps] → el9-x86_64-deps/ (passthrough), gpgcheck=1 vs rocky+EPEL
	//
	// For operator-provided custom URLs (gpgKeyBytes nil), emit a single stanza
	// with gpgcheck=0 (we don't know the remote repo's key setup).
	//
	// repo_gpgcheck=0 on both: repodata/ is not separately signed (createrepo_c
	// output is not gpg-signed in the current bundle — repodata signing is a
	// separate sprint).
	if err := os.MkdirAll(filepath.Join(mountRoot, "etc", "yum.repos.d"), 0o755); err != nil {
		log.Warn().Err(err).Msg("finalize slurm: auto-install: could not create yum.repos.d (non-fatal)")
		return
	}

	repoPath := filepath.Join(mountRoot, "etc", "yum.repos.d", "clustr-slurm.repo")
	var repoContent string
	if isClusterBuiltin {
		// Derive the deps URL from the primary URL by replacing the subdir name.
		// e.g. http://10.99.0.1:8080/repo/el9-x86_64/ → http://10.99.0.1:8080/repo/el9-x86_64-deps/
		// Robust: replace the trailing slash-terminated subpath segment.
		depsURL := strings.Replace(repoURL, "/el9-x86_64/", "/el9-x86_64-deps/", 1)
		if depsURL == repoURL {
			// Fallback for non-standard URL shapes: append -deps before the trailing slash.
			if strings.HasSuffix(repoURL, "/") {
				depsURL = strings.TrimSuffix(repoURL, "/") + "-deps/"
			} else {
				depsURL = repoURL + "-deps"
			}
		}
		repoContent = "[clustr-slurm]\n" +
			"name=clustr Slurm (built and signed by clustr)\n" +
			"baseurl=" + repoURL + "\n" +
			"enabled=1\n" +
			"gpgcheck=1\n" +
			"repo_gpgcheck=0\n" +
			"gpgkey=file:///etc/pki/rpm-gpg/RPM-GPG-KEY-clustr\n" +
			"\n" +
			"[clustr-slurm-deps]\n" +
			"name=clustr Slurm runtime deps (mirrored from Rocky/EPEL, signed upstream)\n" +
			"baseurl=" + depsURL + "\n" +
			"enabled=1\n" +
			"gpgcheck=1\n" +
			"repo_gpgcheck=0\n" +
			"gpgkey=file:///etc/pki/rpm-gpg/RPM-GPG-KEY-rocky-9 file:///etc/pki/rpm-gpg/RPM-GPG-KEY-EPEL-9\n"
	} else {
		// Custom / unknown repo URL: single stanza, gpgcheck=0 (we can't know
		// whether the remote repo uses the clustr key or another key).
		repoContent = "[clustr-slurm]\n" +
			"name=clustr Slurm\n" +
			"baseurl=" + repoURL + "\n" +
			"enabled=1\n" +
			"gpgcheck=0\n" +
			"repo_gpgcheck=0\n"
	}

	if err := os.WriteFile(repoPath, []byte(repoContent), 0o644); err != nil {
		log.Warn().Err(err).Msg("finalize slurm: auto-install: could not write repo file (non-fatal)")
		return
	}
	log.Info().
		Str("repo_file", "/etc/yum.repos.d/clustr-slurm.repo").
		Bool("gpgcheck_clustr", isClusterBuiltin).
		Bool("two_stanza", isClusterBuiltin).
		Msg("finalize slurm: auto-install: repo file written")

	// Step 4: Check whether all required packages are already installed in the
	// chroot before running dnf.  Gold images that were captured with Slurm
	// pre-installed will have all packages present; running dnf install in that
	// case produces a spurious "dnf install failed (non-fatal)" warning even
	// though everything is already in place (NEW-GAP-15).
	//
	// `rpm -q <pkg>...` exits 0 if ALL listed packages are installed, non-zero
	// if any are missing.  We run it inside the chroot so rpm reads the chroot
	// RPM database, not the host's.
	rpmArgs := append([]string{mountRoot, "rpm", "-q"}, pkgs...)
	if rpmOut, rpmErr := exec.CommandContext(ctx, "chroot", rpmArgs...).CombinedOutput(); rpmErr == nil {
		log.Info().Strs("packages", pkgs).
			Msg("finalize slurm: auto-install: slurm packages already present in image, skipping dnf install")
		_ = rpmOut
		return
	}

	// Step 5: Install packages inside chroot (one or more packages missing).
	// --disablerepo='*' prevents dnf from fetching metadata for the default
	// Rocky/EPEL mirrors (unreachable from the deploy initramfs's isolated
	// provisioning network).
	//
	// For the clustr-builtin repo (two-stanza .repo file, GAP-17 hardening):
	//   --enablerepo=clustr-slurm,clustr-slurm-deps enables BOTH stanzas so
	//   that clustr-built RPMs and passthrough dep RPMs are all resolvable.
	//
	// For custom operator URLs (single-stanza .repo file):
	//   --enablerepo=clustr-slurm is sufficient (only one repo stanza exists).
	var enableRepoArg string
	if isClusterBuiltin {
		enableRepoArg = "clustr-slurm,clustr-slurm-deps"
	} else {
		enableRepoArg = "clustr-slurm"
	}
	args := append([]string{mountRoot, "dnf", "install", "-y", "--setopt=install_weak_deps=False",
		"--disablerepo=*", "--enablerepo=" + enableRepoArg}, pkgs...)
	out, err := exec.CommandContext(ctx, "chroot", args...).CombinedOutput()
	if err != nil {
		// Truncate dnf output to last 2 KB for the audit record.
		dnfOut := string(out)
		if len(dnfOut) > 2048 {
			dnfOut = "...(truncated)...\n" + dnfOut[len(dnfOut)-2048:]
		}

		log.Warn().Err(err).
			Str("node_id", nodeID).
			Str("repo_url", repoURL).
			Str("dnf_output", dnfOut).
			Msg("finalize slurm: auto-install: dnf install failed (non-fatal) — check repo URL and image network access during deploy")

		// Notify audit service so operators can query the audit log.
		if auditFn != nil {
			auditFn(ctx, nodeID, AuditActionSlurmInstallFailed,
				fmt.Sprintf("dnf install failed for repo %s: %v\n\nOutput (last 2KB):\n%s", repoURL, err, dnfOut))
		}
		return
	}
	log.Info().Strs("packages", pkgs).Msg("finalize slurm: auto-install: packages installed successfully")

	// Step 5a: Create slurm and munge OS users in the chroot.
	//
	// GAP-NEW-2: clustr's from-source Slurm RPMs have NO %pre scriptlet
	// (verified: `rpm -qp --scripts slurm-*.rpm` returns empty). OpenHPC's RPMs
	// used to create these users via scriptlet at install time — ours do not.
	// Without these users:
	//   • slurmctld.service fails with status=217/USER
	//   • slurmd fails with "Invalid user for SlurmUser slurm"
	//   • munged fails with "Invalid user for MungeUser munge"
	//
	// We use getent passwd to check idempotently — useradd exits non-zero if
	// the user already exists (e.g. captured from a prior image with OpenHPC),
	// so we only call useradd when the account is absent. The `|| true` ensures
	// this remains non-fatal even in edge cases (read-only chroot, locked
	// /etc/passwd, etc.).
	//
	// UIDs are left to the system range (--system picks the next available UID
	// from /etc/login.defs SYS_UID_MIN/MAX). Slurm upstream and OpenHPC both
	// use UID 64030/GID 64030 for slurm and UID 993/GID 991 for munge, but
	// these vary across distros. Using --system keeps us consistent with
	// whatever the chroot's login.defs defines and avoids conflicts with
	// operator-assigned UIDs. If your site requires fixed UIDs, use the
	// SystemAccounts module instead (it writes /etc/passwd entries directly).
	//
	// TODO(GAP-17 hardening): if we add %pre scriptlets to our RPM spec, these
	// useradd calls become redundant and can be removed.
	for _, u := range []struct {
		name    string
		home    string
		comment string
	}{
		{"slurm", "/var/spool/slurm", "Slurm Workload Manager"},
		{"munge", "/var/run/munge", "MUNGE Uid 'N' Gid Emporium"},
	} {
		// Check if the user already exists in the chroot's passwd database.
		chkOut, chkErr := exec.CommandContext(ctx, "chroot", mountRoot, "getent", "passwd", u.name).CombinedOutput()
		if chkErr == nil && len(chkOut) > 0 {
			log.Info().Str("user", u.name).
				Msg("finalize slurm: auto-install: system user already present in chroot, skipping useradd")
			continue
		}
		// User absent — create it.
		addOut, addErr := exec.CommandContext(ctx, "chroot", mountRoot,
			"useradd",
			"--system",
			"--shell", "/sbin/nologin",
			"--home-dir", u.home,
			"--no-create-home",
			"--comment", u.comment,
			u.name,
		).CombinedOutput()
		if addErr != nil {
			log.Warn().Err(addErr).Str("user", u.name).Str("output", string(addOut)).
				Msg("finalize slurm: auto-install: useradd failed (non-fatal) — daemon will fail to start if user is absent")
		} else {
			log.Info().Str("user", u.name).
				Msg("finalize slurm: auto-install: created system user in chroot")
		}
	}

	// Verify both users are now present. Log clearly on failure so operators
	// know exactly what is missing when slurmctld/munged fail to start.
	for _, u := range []string{"slurm", "munge"} {
		if verOut, verErr := exec.CommandContext(ctx, "chroot", mountRoot, "getent", "passwd", u).CombinedOutput(); verErr != nil {
			log.Error().Str("user", u).Str("output", string(verOut)).
				Msg("finalize slurm: auto-install: CRITICAL — system user missing after useradd; daemon will fail with status=217/USER on first boot")
		} else {
			log.Info().Str("user", u).Str("entry", strings.TrimSpace(string(verOut))).
				Msg("finalize slurm: auto-install: system user verified in chroot")
		}
	}

	// Step 6: Verify the installed Slurm version is from the clustr-builtin
	// repo (24.11.4), not the old OpenHPC version (22.05.8). This catches the
	// case where the strip in step 2.5 was a no-op but dnf still resolved to
	// a stale cache or alternative repo.
	//
	// We check slurmctld and slurmd only when they are in the package list for
	// this role — a minimal install (slurm + munge only) won't have them.
	verifyPkgs := []string{"slurm"}
	if hasSlurmdbd {
		verifyPkgs = append(verifyPkgs, "slurm-slurmctld")
	}
	if hasGres {
		verifyPkgs = append(verifyPkgs, "slurm-slurmd")
	}
	for _, pkg := range verifyPkgs {
		verArgs := []string{mountRoot, "rpm", "-q", "--queryformat", "%{VERSION}", pkg}
		verOut, verErr := exec.CommandContext(ctx, "chroot", verArgs...).CombinedOutput()
		if verErr != nil {
			log.Warn().Str("package", pkg).Err(verErr).
				Msg("finalize slurm: auto-install: post-install rpm -q failed (non-fatal)")
			continue
		}
		installedVer := strings.TrimSpace(string(verOut))
		// OpenHPC 22.05.8 packages are the known-bad version from the pre-PR4 images.
		if installedVer == "22.05.8" {
			log.Error().
				Str("package", pkg).
				Str("installed_version", installedVer).
				Str("node_id", nodeID).
				Msg("finalize slurm: auto-install: CONFLICT — installed Slurm is the old OpenHPC 22.05.8 build, not clustr 24.11.4. " +
					"Strip OpenHPC packages from the base image (see docs/imagebuilder.md) and re-image the node.")
			if auditFn != nil {
				auditFn(ctx, nodeID, AuditActionSlurmInstallFailed,
					fmt.Sprintf("post-install version check: %s is %s (expected 24.11.4, got OpenHPC build). "+
						"Rebuild the base image without OpenHPC packages.", pkg, installedVer))
			}
		} else {
			log.Info().Str("package", pkg).Str("version", installedVer).
				Msg("finalize slurm: auto-install: post-install version check passed")
		}
	}
}

// AuditActionSlurmInstallFailed is the audit log action recorded when the
// in-chroot dnf Slurm install step fails during a node deploy.
// Operators can query: GET /api/v1/audit?action=slurm.install.failed
const AuditActionSlurmInstallFailed = "slurm.install.failed"

// extractELVersion returns "9" or "10" (or "") by parsing VERSION_ID from
// /etc/os-release content.  Handles VERSION_ID="9" and VERSION_ID="10.x".
func extractELVersion(osReleaseContent string) string {
	for _, line := range strings.Split(osReleaseContent, "\n") {
		line = strings.TrimSpace(line)
		if !strings.HasPrefix(line, "VERSION_ID=") {
			continue
		}
		val := strings.TrimPrefix(line, "VERSION_ID=")
		val = strings.Trim(val, `"'`)
		// Take major version only: "9", "9.5", "10", "10.1" → "9" or "10"
		if idx := strings.Index(val, "."); idx >= 0 {
			val = val[:idx]
		}
		if val == "9" || val == "10" {
			return val
		}
	}
	return ""
}

// elVersionFromURL returns "9" or "10" (or "") by scanning a repo URL for
// EL version indicators. Recognises two URL patterns:
//
//   1. clustr bundled-repo pattern (PR3): "/repo/el9-x86_64/" → "9"
//      or "/repo/el10-x86_64/" → "10"
//   2. OpenHPC / SchedMD pattern: "EL_9" or "EL_10" substrings (kept as
//      fallback for operator-configured custom repo URLs).
//
// The clustr pattern is checked first. This function must not break when
// given an OpenHPC URL — both patterns may legitimately appear in the URL
// depending on whether the operator overrides slurm_repo_url.
//
// Examples:
//
//	"http://10.99.0.1:8080/repo/el9-x86_64/"  → "9"
//	"http://10.99.0.1:8080/repo/el10-x86_64/" → "10"
//	"https://repos.openhpc.community/OpenHPC/3/EL_9" → "9"
func elVersionFromURL(repoURL string) string {
	lower := strings.ToLower(repoURL)

	// Clustr bundled-repo URL pattern: /repo/el9-x86_64/ or /repo/el10-x86_64/
	// Check el10 before el9 to avoid el10 matching el9 prefix.
	if strings.Contains(lower, "/repo/el10-") {
		return "10"
	}
	if strings.Contains(lower, "/repo/el9-") {
		return "9"
	}

	// OpenHPC / SchedMD URL pattern fallback.
	upper := strings.ToUpper(repoURL)
	if strings.Contains(upper, "EL_10") {
		return "10"
	}
	if strings.Contains(upper, "EL_9") || strings.Contains(upper, "EL9") {
		return "9"
	}
	return ""
}

// When cfg.NoPasswd is true, the rule uses NOPASSWD:ALL.
func writeSudoersDropin(mountRoot string, cfg *api.SudoersNodeConfig) error {
	dir := filepath.Join(mountRoot, "etc", "sudoers.d")
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return fmt.Errorf("mkdir sudoers.d: %w", err)
	}

	var content string
	if cfg.NoPasswd {
		content = fmt.Sprintf("%%%s ALL=(ALL) NOPASSWD:ALL\n", cfg.GroupCN)
	} else {
		content = fmt.Sprintf("%%%s ALL=(ALL) ALL\n", cfg.GroupCN)
	}

	path := filepath.Join(dir, cfg.GroupCN)
	if err := os.WriteFile(path, []byte(content), 0o440); err != nil {
		return fmt.Errorf("write sudoers drop-in: %w", err)
	}
	return nil
}
