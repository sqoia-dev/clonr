func TestSupermicroProviderBinaryMissing(t *testing.T) {
	p := &supermicroProvider{binPath: "/nonexistent/path/sum"}
	ctx := t.Context()

	_, err := p.ReadCurrent(ctx)
	if err == nil {
		t.Fatal("ReadCurrent: expected error when binary missing, got nil")
	}
	if !errors.Is(err, bios.ErrBinaryMissing) {
		t.Errorf("ReadCurrent: got %v, want to wrap ErrBinaryMissing", err)
	}

	// nil changes short-circuits before binary check — nil error expected.
	_, err = p.Apply(ctx, nil)
	if err != nil {
		t.Fatalf("Apply(nil changes): expected nil error, got %v", err)
	}

	// Non-nil changes with absent binary should return ErrBinaryMissing.
	_, err = p.Apply(ctx, []bios.Change{
		{Setting: bios.Setting{Name: "Advanced.Hyper-threading", Value: "Disabled"}, From: "Enabled", To: "Disabled"},
	})
	if err == nil {
		t.Fatal("Apply(non-nil changes) with absent binary: expected error, got nil")
	}
	if !errors.Is(err, bios.ErrBinaryMissing) {
		t.Errorf("Apply: got %v, want to wrap ErrBinaryMissing", err)
	}

	supported, err := p.SupportedSettings(ctx)
	if err != nil {
		t.Fatalf("SupportedSettings with absent binary: expected nil error (non-fatal), got %v", err)
	}
	if supported != nil {
		t.Fatalf("SupportedSettings with absent binary: expected nil slice, got %v", supported)
	}
}

// ─── Diff ────────────────────────────────────────────────────────────────────

func TestSupermicroProviderDiff(t *testing.T) {
	p := &supermicroProvider{binPath: "/nonexistent/sum"}

	current := []bios.Setting{
		{Name: "Advanced.Hyper-threading", Value: "Enabled"},
		{Name: "Boot.Boot Mode Select", Value: "Legacy"},
		{Name: "Advanced.SR-IOV Support", Value: "Disabled"},
	}
	desired := []bios.Setting{
		{Name: "Advanced.Hyper-threading", Value: "Disabled"},    // change
		{Name: "Boot.Boot Mode Select", Value: "Legacy"},         // no change
		{Name: "Advanced.Intel Virtualization Technology", Value: "Enabled"}, // new
	}

	changes, err := p.Diff(desired, current)
	if err != nil {
		t.Fatalf("Diff: %v", err)
	}
	if len(changes) != 2 {
		t.Fatalf("Diff returned %d changes, want 2: %+v", len(changes), changes)
	}

	idx := make(map[string]bios.Change, len(changes))
	for _, c := range changes {
		idx[c.Name] = c
	}

	if c, ok := idx["Advanced.Hyper-threading"]; !ok {
		t.Error("expected change for Hyper-threading, not found")
	} else {
		if c.From != "Enabled" || c.To != "Disabled" {
			t.Errorf("Hyper-threading change: got From=%q To=%q, want From=Enabled To=Disabled", c.From, c.To)
		}
	}

	if c, ok := idx["Advanced.Intel Virtualization Technology"]; !ok {
		t.Error("expected change for Intel VT (new setting), not found")
	} else {
		if c.From != "" || c.To != "Enabled" {
			t.Errorf("VT change: got From=%q To=%q, want From= To=Enabled", c.From, c.To)
		}
	}
}

func TestSupermicroProviderDiffNoDrift(t *testing.T) {
	p := &supermicroProvider{binPath: "/nonexistent/sum"}

	current := []bios.Setting{
		{Name: "Advanced.Hyper-threading", Value: "Enabled"},
	}
	desired := []bios.Setting{
		{Name: "Advanced.Hyper-threading", Value: "Enabled"},
	}

	changes, err := p.Diff(desired, current)
	if err != nil {
		t.Fatalf("Diff: %v", err)
	}
	if len(changes) != 0 {
		t.Errorf("Diff: got %d changes for identical settings, want 0: %+v", len(changes), changes)
	}
}
