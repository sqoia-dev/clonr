func TestParseSumINI_SimpleKeyValue(t *testing.T) {
	input := `[Advanced]
Hyper-threading=Enabled
Intel Virtualization Technology=Enabled

[Boot]
Boot Mode Select=Legacy
`
	got := parseSumINI(input)
	if len(got) != 3 {
		t.Fatalf("got %d settings, want 3: %+v", len(got), got)
	}
	if got[0].Name != "Advanced.Hyper-threading" || got[0].Value != "Enabled" {
		t.Errorf("got[0] = %+v, want Name=Advanced.Hyper-threading Value=Enabled", got[0])
	}
	if got[2].Name != "Boot.Boot Mode Select" || got[2].Value != "Legacy" {
		t.Errorf("got[2] = %+v, want Name=Boot.Boot Mode Select Value=Legacy", got[2])
	}
}

