// RegisterNode handles POST /api/v1/nodes/register.
// Called by the clustr client on first PXE boot. Creates a NodeConfig stub if
// the MAC is new, or updates the hardware_profile if the node already exists.
// Returns the NodeConfig and an action directive ("deploy" or "wait").
func (h *NodesHandler) RegisterNode(w http.ResponseWriter, r *http.Request) {
	const maxRegisterBodyBytes = 1 << 20 // 1 MiB
	r.Body = http.MaxBytesReader(w, r.Body, maxRegisterBodyBytes)

	var req api.RegisterRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		if err.Error() == "http: request body too large" {
			http.Error(w, "request body too large (max 1MB)", http.StatusRequestEntityTooLarge)
			return
		}
		writeValidationError(w, "invalid JSON body")
		return
	}

	if len(req.HardwareProfile) == 0 {
		writeValidationError(w, "hardware_profile is required")
		return
	}

	// Extract primary MAC and hostname from the hardware profile.
	primaryMAC, hwHostname := extractNodeIdentity(req.HardwareProfile)
	if primaryMAC == "" {
		writeValidationError(w, "hardware_profile must contain at least one NIC with a MAC address")
		return
	}

	// Determine the hostname for this registration pass.
	// Priority: hardware-reported hostname > auto-generated.
	// Auto-generation only applies when the hardware profile carries no hostname.
	hostname := hwHostname
	hostnameAuto := false
	if hostname == "" {
		base := autoHostname(primaryMAC)
		hostname = base
		// Resolve collisions: if the base name is taken by a different node,
		// append a numeric suffix (e.g. clustr-36e92f-2) until we find a free slot.
		for suffix := 2; ; suffix++ {
			exists, err := h.DB.HostnameExists(r.Context(), hostname)
			if err != nil {
				log.Warn().Err(err).Str("hostname", hostname).
					Msg("register node: hostname collision check failed — using base name")
				hostname = base
				break
			}
			if !exists {
				break
			}
			hostname = fmt.Sprintf("%s-%d", base, suffix)
		}
		hostnameAuto = true
		log.Info().Str("mac", primaryMAC).Str("hostname", hostname).
			Msg("register node: auto-generated hostname")
	}

	// Build FQDN for new nodes when none is reported by hardware.
	fqdn := hostname + "." + defaultDomain()

	now := time.Now().UTC()
	stub := api.NodeConfig{
		ID:               uuid.New().String(),
		Hostname:         hostname,
		HostnameAuto:     hostnameAuto,
		FQDN:             fqdn,
		PrimaryMAC:       primaryMAC,
		Interfaces:       []api.InterfaceConfig{},
		SSHKeys:          []string{},
		Tags:             []string{},
		Groups:           []string{}, // deprecated alias
		CustomVars:       map[string]string{},
		HardwareProfile:  req.HardwareProfile,
		DetectedFirmware: req.DetectedFirmware,
		CreatedAt:        now,
		UpdatedAt:        now,
	}

	// Auto-populate interfaces from hardware NICs + DHCP lease before upserting.
	// This pre-fills the Network tab for new nodes so the admin doesn't have to
	// manually enter each NIC after registration.
	autoIfaces := buildAutoInterfaces(req.HardwareProfile, h.LookupDHCPLease, h.DHCPSubnetCIDR, h.ServerIP)
	if len(autoIfaces) > 0 {
		stub.Interfaces = autoIfaces
	}

	nodeCfg, err := h.DB.UpsertNodeByMAC(r.Context(), stub)
	if err != nil {
		log.Error().Err(err).Str("mac", primaryMAC).Msg("register node: upsert failed")
		writeError(w, err)
		return
	}

	// For existing nodes that re-registered with empty interfaces, apply auto-population
	// now so the Network tab is pre-filled. We never overwrite manually-configured
	// interfaces — only fill in when the node currently has none.
	if len(nodeCfg.Interfaces) == 0 && len(autoIfaces) > 0 {
		if err := h.DB.SetNodeInterfaces(r.Context(), nodeCfg.ID, autoIfaces); err != nil {
			log.Warn().Err(err).Str("node_id", nodeCfg.ID).
				Msg("register node: failed to auto-populate interfaces (non-fatal)")
		} else {
			nodeCfg.Interfaces = autoIfaces
			log.Info().Str("node_id", nodeCfg.ID).Int("count", len(autoIfaces)).
				Msg("register node: auto-populated interfaces from hardware discovery + DHCP")
		}
	}

	action := "wait"
	if nodeCfg.BaseImageID != "" {
		action = "deploy"
	}
	// A pending reimage overrides the normal wait/deploy logic: always deploy.
	// The reimage orchestrator set this flag after firing SetNextBoot(PXE) +
	// PowerCycle — the node is here because it was told to come back for a
	// fresh image.
	var dryRun bool
	if nodeCfg.ReimagePending {
		action = "deploy"
		// Look up the active reimage request to surface dry_run to the client.
		if activeReq, err := h.DB.GetActiveReimageForNode(r.Context(), nodeCfg.ID); err == nil && activeReq != nil {
			dryRun = activeReq.DryRun
		}
	}

	log.Info().Str("mac", primaryMAC).Str("hostname", nodeCfg.Hostname).
		Bool("hostname_auto", nodeCfg.HostnameAuto).
		Bool("reimage_pending", nodeCfg.ReimagePending).
		Bool("dry_run", dryRun).
		Str("action", action).Msg("node registered")

	// Pre-merge group extra mounts so the deploy client's ExtraMounts is complete.
	nodeCfg = mergeGroupExtraMounts(r.Context(), h.DB, nodeCfg)

	// Populate LDAP client config if the module is enabled. This allows the
	// deploy agent to write sssd.conf + CA into the deployed filesystem.
	if h.LDAPNodeConfig != nil && action == "deploy" {
		if ldapCfg, err := h.LDAPNodeConfig(r.Context()); err != nil {
			log.Warn().Err(err).Str("node_id", nodeCfg.ID).Msg("register node: failed to fetch LDAP config (non-fatal)")
		} else {
			nodeCfg.LDAPConfig = ldapCfg
		}
	}

	// Populate system accounts config if any are defined. The deploy agent injects
	// these into /etc/passwd, /etc/group, and /etc/shadow during finalization.
	if h.SystemAccountsConfig != nil && action == "deploy" {
		if saCfg, err := h.SystemAccountsConfig(r.Context()); err != nil {
			log.Warn().Err(err).Str("node_id", nodeCfg.ID).Msg("register node: failed to fetch system accounts config (non-fatal)")
		} else {
			nodeCfg.SystemAccounts = saCfg
		}
	}

	// Populate network config if the node's group has a network profile assigned.
	// The deploy agent writes NM keyfiles for bonds, VLANs, and IPoIB during finalization.
	if h.NetworkConfig != nil && action == "deploy" && nodeCfg.GroupID != "" {
		if netCfg, err := h.NetworkConfig(r.Context(), nodeCfg.GroupID); err != nil {
			log.Warn().Err(err).Str("node_id", nodeCfg.ID).Msg("register node: failed to fetch network config (non-fatal)")
		} else {
			nodeCfg.NetworkConfig = netCfg
		}
	}

	// Populate Slurm config if the module is enabled. The deploy agent writes
	// rendered Slurm config files and enables Slurm/munge services during finalization.
	if h.SlurmNodeConfig != nil && action == "deploy" {
		if slurmCfg, err := h.SlurmNodeConfig(r.Context(), nodeCfg.ID); err != nil {
			log.Warn().Err(err).Str("node_id", nodeCfg.ID).Msg("register node: failed to fetch Slurm config (non-fatal)")
		} else {
			nodeCfg.SlurmConfig = slurmCfg
		}
	}

	// Populate sudoers config if the feature is enabled. The deploy agent writes
	// /etc/sudoers.d/<group_cn> during finalization so LDAP group members can sudo.
	if h.SudoersNodeConfig != nil && action == "deploy" {
		if sudoersCfg, err := h.SudoersNodeConfig(r.Context()); err != nil {
			log.Warn().Err(err).Str("node_id", nodeCfg.ID).Msg("register node: failed to fetch sudoers config (non-fatal)")
		} else {
			nodeCfg.SudoersConfig = sudoersCfg
		}
	}

	// S4-11: Merge per-deployment inject_vars from the active reimage request into
	// the node's CustomVars. inject_vars are ephemeral — they exist only in the
	// reimage_requests row and are NOT written back to node_configs, so they never
	// affect future deploys. Keys from inject_vars override matching node-level keys.
	if action == "deploy" && nodeCfg.ReimagePending {
		if injectVars, err := h.DB.GetInjectVarsForActiveReimage(r.Context(), nodeCfg.ID); err != nil {
			log.Warn().Err(err).Str("node_id", nodeCfg.ID).Msg("register node: failed to fetch inject_vars (non-fatal)")
		} else if len(injectVars) > 0 {
			merged := make(map[string]string, len(nodeCfg.CustomVars)+len(injectVars))
			for k, v := range nodeCfg.CustomVars {
				merged[k] = v
			}
			for k, v := range injectVars {
				merged[k] = v // inject_vars win on collision
			}
			nodeCfg.CustomVars = merged
			log.Info().Str("node_id", nodeCfg.ID).Int("inject_count", len(injectVars)).
				Msg("register node: inject_vars merged into CustomVars for this deployment")
		}
	}

	// Populate ClusterHosts so the deploy agent can write /etc/hosts with all
	// cluster node IPs. Only inject on deploy actions — wait responses don't need it.
	if action == "deploy" {
		if allNodes, err := h.DB.ListNodeConfigs(r.Context(), ""); err != nil {
			log.Warn().Err(err).Str("node_id", nodeCfg.ID).Msg("register node: failed to list nodes for cluster hosts (non-fatal)")
		} else {
			var clusterHosts []api.HostEntry
			for _, n := range allNodes {
				// Only include nodes that have at least one interface with an IP.
				if len(n.Interfaces) == 0 || n.Interfaces[0].IPAddress == "" {
					continue
				}
				ip := extractIP(n.Interfaces[0].IPAddress)
				if ip == "" {
					continue
				}
				entry := api.HostEntry{
					IP:       ip,
					Hostname: n.Hostname,
				}
				if n.FQDN != "" && n.FQDN != n.Hostname {
					entry.FQDN = n.FQDN
				}
				clusterHosts = append(clusterHosts, entry)
			}
			// Add the clustr server itself so nodes can resolve "clustr" and
			// "clustr-server" without DNS. "clustr-server" is required for LDAP
			// resolution: sssd.conf contains ldap_uri = ldaps://clustr-server:636
			// (derived from os.Hostname() on the server host).
			if h.ServerIP != "" {
				domain := defaultDomain()
				clusterHosts = append(clusterHosts, api.HostEntry{
					IP:       h.ServerIP,
					Hostname: "clustr",
					FQDN:     "clustr." + domain,
					Aliases:  []string{"clustr-server"},
				})
			}
			if len(clusterHosts) > 0 {
				nodeCfg.ClusterHosts = clusterHosts
				log.Info().Int("entries", len(clusterHosts)).Str("node_id", nodeCfg.ID).
					Msg("register node: injecting cluster hosts into NodeConfig")
			}
		}
	}

	writeJSON(w, http.StatusOK, api.RegisterResponse{
		NodeConfig: &nodeCfg,
		Action:     action,
		DryRun:     dryRun,
	})
}

// DeployComplete handles POST /api/v1/nodes/:id/deploy-complete.
// Called by the clustr CLI after a successful deployment finalize.
// Sets deploy_completed_preboot_at and clears reimage_pending, transitioning the
// node to NodeStateDeployedPreboot. Subsequent PXE boots will return "exit" (disk boot).
// Also transitions any active reimage record to succeeded.
func (h *NodesHandler) DeployComplete(w http.ResponseWriter, r *http.Request) {
	id := chi.URLParam(r, "id")

	// Confirm node exists before updating.
	if _, err := h.DB.GetNodeConfig(r.Context(), id); err != nil {
		writeError(w, err)
		return
	}

	if err := h.DB.RecordDeploySucceeded(r.Context(), id); err != nil {
		log.Error().Err(err).Str("node_id", id).Msg("deploy-complete: record succeeded")
		writeError(w, err)
		return
	}

	// Transition the active reimage record (if any) to complete.
	active, err := h.DB.GetActiveReimageForNode(r.Context(), id)
	if err != nil {
		log.Warn().Err(err).Str("node_id", id).Msg("deploy-complete: could not look up active reimage (non-fatal)")
	} else if active != nil {
		if dbErr := h.DB.UpdateReimageRequestStatus(r.Context(), active.ID, api.ReimageStatusComplete, ""); dbErr != nil {
			log.Warn().Err(dbErr).Str("reimage_id", active.ID).Msg("deploy-complete: could not update reimage record (non-fatal)")
		} else {
			log.Info().Str("reimage_id", active.ID).Str("node_id", id).Msg("deploy-complete: reimage record transitioned to complete")
			metrics.DeployTotal.WithLabelValues("complete").Inc()
		}
	}

	log.Info().Str("node_id", id).Msg("deploy-complete: node marked deployed")

	// S4-2: Fire webhook for deploy.complete.
	if h.WebhookDispatcher != nil {
		nodeForWebhook, _ := h.DB.GetNodeConfig(r.Context(), id)
		h.WebhookDispatcher.Dispatch(r.Context(), webhook.EventDeployComplete, webhook.Payload{
			NodeID:  id,
			ImageID: nodeForWebhook.BaseImageID,
		})
	}

	// Record LDAP configuration for this node if the module is enabled.
	// This locks the base DN on the first node to be configured.
	if h.RecordNodeLDAPConfigured != nil {
		// Re-read the node to get the current config hash. If the node had LDAP
		// config injected at registration time the deploy agent wrote sssd.conf; we
		// record that fact here using the node's hostname as a stable identifier.
		// The hash is computed server-side from the current LDAP config so it stays
		// in sync even if the config changed between registration and deploy-complete.
		//
		// Non-fatal: LDAP recording failure should not fail the deploy-complete response.
		if _, err := h.DB.GetNodeConfig(r.Context(), id); err == nil {
			if err := h.RecordNodeLDAPConfigured(r.Context(), id, "deploy-complete"); err != nil {
				log.Warn().Err(err).Str("node_id", id).Msg("deploy-complete: LDAP RecordNodeConfigured failed (non-fatal)")
			}
		}
	}

	w.WriteHeader(http.StatusNoContent)
}

// DeployFailed handles POST /api/v1/nodes/:id/deploy-failed.
// Called by the clustr CLI after a deployment failure.
// Sets last_deploy_failed_at, transitioning the node to NodeStateFailed.
// Does not clear reimage_pending -- admin decides whether to retry or cancel.
// Accepts an optional DeployFailedPayload body to capture exit code / phase detail.
func (h *NodesHandler) DeployFailed(w http.ResponseWriter, r *http.Request) {
	id := chi.URLParam(r, "id")

	// Parse optional structured payload — tolerate missing/empty body.
	var payload api.DeployFailedPayload
	if r.ContentLength != 0 {
		if err := json.NewDecoder(r.Body).Decode(&payload); err != nil {
			log.Warn().Err(err).Str("node_id", id).Msg("deploy-failed: could not parse payload (ignored)")
		}
	}

	// Confirm node exists before updating.
	if _, err := h.DB.GetNodeConfig(r.Context(), id); err != nil {
		writeError(w, err)
		return
	}

	if err := h.DB.RecordDeployFailed(r.Context(), id); err != nil {
		log.Error().Err(err).Str("node_id", id).Msg("deploy-failed: record failed")
		writeError(w, err)
		return
	}

	// Transition the active reimage record (if any) to failed with exit detail.
	active, err := h.DB.GetActiveReimageForNode(r.Context(), id)
	if err != nil {
		log.Warn().Err(err).Str("node_id", id).Msg("deploy-failed: could not look up active reimage (non-fatal)")
	} else if active != nil {
		msg := payload.Message
		if msg == "" {
			msg = "deploy agent reported failure"
		}
		if dbErr := h.DB.UpdateReimageRequestFailed(r.Context(), active.ID, msg, payload.ExitCode, payload.ExitName, payload.Phase); dbErr != nil {
			log.Warn().Err(dbErr).Str("reimage_id", active.ID).Msg("deploy-failed: could not update reimage record (non-fatal)")
		} else {
			log.Info().
				Str("reimage_id", active.ID).
				Str("node_id", id).
				Int("exit_code", payload.ExitCode).
				Str("exit_name", payload.ExitName).
				Str("phase", payload.Phase).
				Msg("deploy-failed: reimage record transitioned to failed")
			metrics.DeployTotal.WithLabelValues("failed").Inc()
		}
	}

	log.Warn().
		Str("node_id", id).
		Int("exit_code", payload.ExitCode).
		Str("exit_name", payload.ExitName).
		Str("phase", payload.Phase).
		Msg("deploy-failed: node marked failed")

	// S4-2: Fire webhook for deploy.failed.
	if h.WebhookDispatcher != nil {
		nodeForWebhook, _ := h.DB.GetNodeConfig(r.Context(), id)
		h.WebhookDispatcher.Dispatch(r.Context(), webhook.EventDeployFailed, webhook.Payload{
			NodeID:  id,
			ImageID: nodeForWebhook.BaseImageID,
		})
	}

	w.WriteHeader(http.StatusNoContent)
}

// VerifyBoot handles POST /api/v1/nodes/:id/verify-boot. ADR-0008.
//
// Called by the deployed OS on first boot (via clustr-verify-boot.service systemd
// oneshot). Proves the bootloader, kernel, and systemd all started successfully.
//
// Auth: node-scoped token only. The token's bound node_id must match the URL {id}.
// Admin-scoped keys are NOT accepted here — enforced by requireNodeOwnership
// middleware in the router registration. This ensures only the node itself can
// report its own boot (the token written to /etc/clustr/node-token at finalize time
// is node-scoped and bound to that specific node's ID).
//
// State transitions:
//
//	deploy_completed_preboot_at set, deploy_verified_booted_at NULL
//	→ deploy_verified_booted_at = now()  (first call)
//	→ deploy_verified_booted_at unchanged (subsequent calls; only last_seen_at updates)
//
// Returns 204 No Content on success.
func (h *NodesHandler) VerifyBoot(w http.ResponseWriter, r *http.Request) {
	id := chi.URLParam(r, "id")

	// Parse the phone-home payload. Tolerate a missing body gracefully.
	var payload api.VerifyBootRequest
	if r.ContentLength != 0 {
		if err := json.NewDecoder(r.Body).Decode(&payload); err != nil {
			log.Warn().Err(err).Str("node_id", id).Msg("verify-boot: could not parse payload (ignored)")
		}
	}

	// Confirm node exists before updating.
	if _, err := h.DB.GetNodeConfig(r.Context(), id); err != nil {
		writeError(w, err)
		return
	}

	// firstTime is true only on the first phone-home for this deploy cycle
	// (deploy_verified_booted_at was NULL and is now set). Subsequent phone-homes
	// from clientd return firstTime=false so we can gate one-shot side effects.
	firstTime, err := h.DB.RecordVerifyBooted(r.Context(), id)
	if err != nil {
		log.Error().Err(err).Str("node_id", id).Msg("verify-boot: db update failed")
		writeError(w, err)
		return
	}

	log.Info().
		Str("node_id", id).
		Str("hostname", payload.Hostname).
		Str("kernel_version", payload.KernelVersion).
		Str("systemctl_state", payload.SystemctlState).
		Str("os_release", payload.OSRelease).
		Float64("uptime_seconds", payload.UptimeSeconds).
		Msgf("verify-boot: node %s (%s) verified booted: kernel=%s, systemctl=%s",
			id, payload.Hostname, payload.KernelVersion, payload.SystemctlState)

	// Flip the persistent boot order back to disk-first on the FIRST verify-boot
	// only. This is a no-op on IPMI (override was already one-shot) but is
	// critical on Proxmox where SetNextBoot wrote a persistent PXE-first config.
	// Gating on firstTime prevents the Proxmox stop+start from firing on every
	// subsequent clientd phone-home, which would cause an infinite stop/start loop.
	// Best-effort: errors are logged but do not fail the 204 response -- the node
	// is already deployed and the worst case is it PXE-boots cleanly on next cycle.
	// See docs/boot-architecture.md §10.
	if firstTime && h.FlipToDiskFirst != nil {
		if err := h.FlipToDiskFirst(r.Context(), id); err != nil {
			log.Warn().Err(err).Str("node_id", id).
				Msg("verify-boot: FlipToDiskFirst failed (non-fatal -- node is deployed; persistent boot order may still be PXE-first)")
		} else {
			log.Info().Str("node_id", id).
				Msg("verify-boot: persistent boot order flipped to disk-first")
		}
	}

	w.WriteHeader(http.StatusNoContent)
}

// buildAutoInterfaces parses the hardware profile and constructs InterfaceConfig
// entries for each non-loopback NIC with a real MAC. If LookupDHCPLease is set
// and returns a lease for the NIC's MAC, the IP/gateway are pre-filled.
// subnetCIDR is the provisioning subnet prefix length (0 defaults to 24).
// serverIP is used as the gateway when a DHCP lease is found.
func buildAutoInterfaces(hwProfile json.RawMessage, lookupLease func(mac string) net.IP, subnetCIDR int, serverIP string) []api.InterfaceConfig {
	if subnetCIDR <= 0 || subnetCIDR > 30 {
		subnetCIDR = 24
	}

	var hw struct {
		NICs []struct {
			Name string `json:"Name"`
			MAC  string `json:"MAC"`
		} `json:"NICs"`
	}
	if err := json.Unmarshal(hwProfile, &hw); err != nil || len(hw.NICs) == 0 {
		return nil
	}

	var ifaces []api.InterfaceConfig
	for _, nic := range hw.NICs {
		if nic.Name == "lo" || nic.MAC == "" || nic.MAC == "00:00:00:00:00:00" {
			continue
		}
		mac := strings.ToLower(nic.MAC)
		iface := api.InterfaceConfig{
			MACAddress: mac,
			Name:       nic.Name,
		}
		if lookupLease != nil {
			if leaseIP := lookupLease(mac); leaseIP != nil {
				iface.IPAddress = fmt.Sprintf("%s/%d", leaseIP.String(), subnetCIDR)
				if serverIP != "" {
					iface.Gateway = serverIP
				}
			}
		}
		ifaces = append(ifaces, iface)
	}
	return ifaces
}

// extractNodeIdentity parses the hardware profile JSON to find the primary MAC
// and hostname. Returns empty strings on any parse failure.
func extractNodeIdentity(profile []byte) (mac, hostname string) {
	var hw struct {
		Hostname string `json:"Hostname"`
		NICs     []struct {
			Name  string `json:"Name"`
			MAC   string `json:"MAC"`
			State string `json:"State"`
		} `json:"NICs"`
	}
	if err := json.Unmarshal(profile, &hw); err != nil {
		return "", ""
	}

	hostname = hw.Hostname

	// Pick first non-loopback NIC with a real MAC.
	for _, nic := range hw.NICs {
		if nic.Name == "lo" || nic.MAC == "" || nic.MAC == "00:00:00:00:00:00" {
			continue
		}
		return nic.MAC, hostname
	}
	return "", hostname
}

// batchNodeSpec is the per-row shape accepted by BatchCreateNodes.
type batchNodeSpec struct {
	Hostname    string            `json:"hostname"`
	FQDN        string            `json:"fqdn"`
	PrimaryMAC  string            `json:"primary_mac"`
	BaseImageID string            `json:"base_image_id"`
	Tags        []string          `json:"tags"`
	Groups      []string          `json:"groups"` // deprecated alias
	KernelArgs  string            `json:"kernel_args"`
	Interfaces  []api.InterfaceConfig `json:"interfaces"`
}

// BatchCreateNodes handles POST /api/v1/nodes/batch — BULK-1.
//
// Request body: {"nodes": [{hostname, primary_mac, base_image_id, tags, notes}, ...]}
//
// Returns per-row results: {index, status: "created"|"failed", id?, error?}.
// NOT all-or-nothing: partial success is allowed.
func (h *NodesHandler) BatchCreateNodes(w http.ResponseWriter, r *http.Request) {
	var req struct {
		Nodes []batchNodeSpec `json:"nodes"`
	}
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		writeValidationError(w, "invalid JSON body")
		return
	}
	if len(req.Nodes) == 0 {
		writeValidationError(w, "nodes array is empty")
		return
	}
	if len(req.Nodes) > 500 {
		writeValidationError(w, "nodes array exceeds maximum of 500 entries")
		return
	}

	type rowResult struct {
		Index  int    `json:"index"`
		Status string `json:"status"`
		ID     string `json:"id,omitempty"`
		Error  string `json:"error,omitempty"`
	}

	results := make([]rowResult, len(req.Nodes))
	ctx := r.Context()
	now := time.Now().UTC()

	actorID, actorLabel := "", ""
	if h.GetActorInfo != nil {
		actorID, actorLabel = h.GetActorInfo(r)
	}

	for i, n := range req.Nodes {
		if n.Hostname == "" {
			results[i] = rowResult{Index: i, Status: "failed", Error: "hostname is required"}
			continue
		}
		if n.PrimaryMAC == "" {
			results[i] = rowResult{Index: i, Status: "failed", Error: "primary_mac is required"}
			continue
		}
		n.PrimaryMAC = strings.ToLower(n.PrimaryMAC)

		// Normalise base_image_id: use empty string if not provided.
		if n.BaseImageID != "" {
			if _, err := h.DB.GetBaseImage(ctx, n.BaseImageID); err != nil {
				results[i] = rowResult{Index: i, Status: "failed", Error: fmt.Sprintf("base_image_id %q not found", n.BaseImageID)}
				continue
			}
		}

		tags := n.Tags
		if len(tags) == 0 && len(n.Groups) > 0 {
			tags = n.Groups
		}
		if tags == nil {
			tags = []string{}
		}

		cfg := api.NodeConfig{
			ID:          uuid.New().String(),
			Hostname:    n.Hostname,
			FQDN:        n.FQDN,
			PrimaryMAC:  n.PrimaryMAC,
			Tags:        tags,
			Groups:      tags,
			BaseImageID: n.BaseImageID,
			Interfaces:  n.Interfaces,
			KernelArgs:  n.KernelArgs,
			CustomVars:  map[string]string{},
			SSHKeys:     []string{},
			CreatedAt:   now,
			UpdatedAt:   now,
		}
		if cfg.Interfaces == nil {
			cfg.Interfaces = []api.InterfaceConfig{}
		}

		if err := h.DB.CreateNodeConfig(ctx, cfg); err != nil {
			results[i] = rowResult{Index: i, Status: "failed", Error: err.Error()}
			continue
		}

		if h.Audit != nil {
			h.Audit.Record(ctx, actorID, actorLabel, db.AuditActionNodeCreate, "node", cfg.ID,
				r.RemoteAddr, nil, map[string]string{
					"hostname": cfg.Hostname, "primary_mac": cfg.PrimaryMAC, "batch": "true",
				})
		}

		results[i] = rowResult{Index: i, Status: "created", ID: cfg.ID}
	}

	writeJSON(w, http.StatusOK, map[string]any{"results": results})
}
