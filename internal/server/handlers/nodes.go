// sortNodeConfigs sorts a slice of NodeConfigs by the given column name.
// Valid columns: hostname, status, last_deploy, group. Unrecognised columns are no-ops.
func sortNodeConfigs(nodes []api.NodeConfig, col string, desc bool) {
	less := func(i, j int) bool {
		a, b := nodes[i], nodes[j]
		var cmp int
		switch col {
		case "hostname":
			cmp = strings.Compare(strings.ToLower(a.Hostname), strings.ToLower(b.Hostname))
		case "status":
			cmp = strings.Compare(string(a.State()), string(b.State()))
		case "last_deploy":
			// S6-8: sort by deploy_completed_preboot_at (canonical ADR-0008 field).
			ta := a.DeployCompletedPrebootAt
			tb := b.DeployCompletedPrebootAt
			if ta == nil && tb == nil {
				cmp = 0
			} else if ta == nil {
				cmp = -1
			} else if tb == nil {
				cmp = 1
			} else if ta.Before(*tb) {
				cmp = -1
			} else if ta.After(*tb) {
				cmp = 1
			} else {
				cmp = 0
			}
		case "group":
			cmp = strings.Compare(strings.ToLower(a.GroupID), strings.ToLower(b.GroupID))
		default:
			cmp = 0
		}
		if desc {
			return cmp > 0
		}
		return cmp < 0
	}
	sort.SliceStable(nodes, less)
}

// extractIP returns just the IP from a CIDR string like "10.0.1.50/24".
// Returns the input unchanged if no slash is present.
func extractIP(cidr string) string {
	if idx := strings.Index(cidr, "/"); idx >= 0 {
		return cidr[:idx]
	}
	return cidr
}

// autoHostnameRe matches hostnames that were auto-generated by clustr.
// Pattern: clustr-<6 hex digits>, e.g. "clustr-36e92f".
var autoHostnameRe = regexp.MustCompile(`^clustr-[0-9a-f]{6}$`)

// defaultDomain returns the domain to use when building auto-generated FQDNs.
// Override by setting the CLUSTR_DEFAULT_DOMAIN environment variable.
func defaultDomain() string {
	if d := os.Getenv("CLUSTR_DEFAULT_DOMAIN"); d != "" {
		return d
	}
	return "clustr.local"
}

// autoHostname generates a deterministic hostname from the last 6 hex chars of
// the MAC address (last 3 octets, colons stripped).
// E.g. "bc:24:11:36:e9:2f" → "clustr-36e92f".
// Using 6 chars gives 16.7M unique values vs 65,536 with 4 chars.
func autoHostname(mac string) string {
	stripped := strings.ReplaceAll(mac, ":", "")
	if len(stripped) >= 6 {
		return "clustr-" + stripped[len(stripped)-6:]
	}
	return "clustr-" + stripped
}

// NodesHandler handles all /api/v1/nodes routes.
type NodesHandler struct {
	DB    *db.DB
	Audit *db.AuditService
	// GetActorInfo returns (actorID, actorLabel) for the current request.
	GetActorInfo func(r *http.Request) (id, label string)
	// FlipToDiskFirst, when non-nil, is called by VerifyBoot after recording the
	// deployed_verified state transition. It should call
	// SetPersistentBootOrder([BootDisk, BootPXE]) on the node's power provider.
	// This flips the Proxmox VM boot order back to disk-first after a successful
	// deploy (the reimage path set it to PXE-first via SetNextBoot). On IPMI
	// providers this is a harmless no-op. Best-effort: errors are logged but do
	// not fail the verify-boot response — the node is already deployed.
	// See docs/boot-architecture.md §10.
	FlipToDiskFirst func(ctx context.Context, nodeID string) error
	// LDAPNodeConfig, when non-nil, is called during RegisterNode to inject the
	// current LDAP client config into the NodeConfig returned to the deploy agent.
	// Returns nil if the LDAP module is disabled or not ready.
	LDAPNodeConfig func(ctx context.Context) (*api.LDAPNodeConfig, error)
	// RecordNodeLDAPConfigured, when non-nil, is called from DeployComplete to record
	// that a node's LDAP client config was applied and to lock the base DN on first use.
	RecordNodeLDAPConfigured func(ctx context.Context, nodeID, configHash string) error
	// SystemAccountsConfig returns the current set of system accounts/groups for
	// embed in the NodeConfig returned to the deploy agent. Returns nil if none defined.
	SystemAccountsConfig func(ctx context.Context) (*api.SystemAccountsNodeConfig, error)
	// NetworkConfig, when non-nil, is called during RegisterNode to resolve the
	// network profile for the node's group. Returns nil if no profile is assigned.
	NetworkConfig func(ctx context.Context, groupID string) (*api.NetworkNodeConfig, error)
	// SlurmNodeConfig, when non-nil, is called during RegisterNode to inject the
	// rendered Slurm config for the specific node into the NodeConfig returned to
	// the deploy agent. Returns nil if the Slurm module is disabled or not ready.
	SlurmNodeConfig func(ctx context.Context, nodeID string) (*api.SlurmNodeConfig, error)
	// SudoersNodeConfig, when non-nil, is called during RegisterNode to inject the
	// sudoers drop-in config into the NodeConfig returned to the deploy agent.
	// Returns nil if the sudoers feature is disabled or LDAP is not ready.
	SudoersNodeConfig func(ctx context.Context) (*api.SudoersNodeConfig, error)
	// LookupDHCPLease returns the DHCP-assigned IP for a MAC, or nil if no active lease.
	// Used during RegisterNode to auto-populate InterfaceConfig from hardware discovery.
	// May be nil — if so, DHCP lookup is skipped and interfaces are not auto-populated.
	LookupDHCPLease func(mac string) net.IP
	// DHCPSubnetCIDR is the prefix length of the PXE provisioning subnet (e.g. 24).
	// Used when constructing CIDR strings for auto-populated InterfaceConfigs.
	// Defaults to 24 when zero.
	DHCPSubnetCIDR int
	// ServerIP is the clustr server's own IP address (from CLUSTR_PXE_SERVER_IP).
	// When non-empty, it is included in ClusterHosts injected during RegisterNode so
	// deployed nodes can resolve "clustr" in /etc/hosts without depending on DNS.
	ServerIP string
	// WebhookDispatcher, when non-nil, is called to fan out webhook events on
	// deploy-complete, deploy-failed, and verify-boot callbacks (S4-2).
	WebhookDispatcher *webhook.Dispatcher
}

// sanitizeNodeConfig returns cfg with sensitive fields in PowerProvider and BMC
// redacted. Call this on every outbound NodeConfig so credentials never leak in responses.
func sanitizeNodeConfig(cfg api.NodeConfig) api.NodeConfig {
	cfg.PowerProvider = cfg.PowerProvider.Sanitize()
	if cfg.BMC != nil {
		bmc := *cfg.BMC
		bmc.Password = ""
		cfg.BMC = &bmc
	}
	return cfg
}

// sanitizeNodeConfigs applies sanitizeNodeConfig to a slice in place.
func sanitizeNodeConfigs(cfgs []api.NodeConfig) []api.NodeConfig {
	for i := range cfgs {
		cfgs[i] = sanitizeNodeConfig(cfgs[i])
	}
	return cfgs
}

// ListNodes handles GET /api/v1/nodes
// Accepts optional ?page= and ?per_page= (default 50) for pagination.
// When pagination params are absent the full list is returned (backward compatible).
// S5-3: accepts ?sort=hostname|status|last_deploy|group and ?dir=asc|desc.
func (h *NodesHandler) ListNodes(w http.ResponseWriter, r *http.Request) {
	baseImageID := r.URL.Query().Get("base_image_id")
	search := strings.TrimSpace(r.URL.Query().Get("search"))
	sortCol := r.URL.Query().Get("sort")
	sortDir := r.URL.Query().Get("dir")
	// TAG-2: collect all ?tag= values (multiple allowed, AND semantics).
	tagFilter := r.URL.Query()["tag"]
	rawPage, rawPerPage, paging := parsePaginationQuery(r)

	nodes, err := h.DB.SearchNodeConfigs(r.Context(), baseImageID, search, tagFilter)
	if err != nil {
		log.Error().Err(err).Msg("list nodes")
		writeError(w, err)
		return
	}
	if nodes == nil {
		nodes = []api.NodeConfig{}
	}
	nodes = sanitizeNodeConfigs(nodes)

	// S5-3: server-side sort by requested column.
	if sortCol != "" {
		desc := strings.ToLower(sortDir) == "desc"
		sortNodeConfigs(nodes, sortCol, desc)
	}

	total := len(nodes)
	resp := api.ListNodesResponse{Total: total}
	if paging {
		start, end, p := paginate(total, rawPage, rawPerPage)
		resp.Nodes = nodes[start:end]
		resp.Page = p.page
		resp.PerPage = p.perPage
		// NextCursor is the next page number, 0 means no more pages.
		if end < total {
			resp.NextCursor = p.page + 1
		}
	} else {
		resp.Nodes = nodes
	}
	// S6-7: signal that the "groups" field in NodeConfig is deprecated and will
	// be removed in v1.1.
	setNodeConfigSunsetHeader(w)
	writeJSON(w, http.StatusOK, resp)
}

// CreateNode handles POST /api/v1/nodes
func (h *NodesHandler) CreateNode(w http.ResponseWriter, r *http.Request) {
	var req api.CreateNodeConfigRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		writeValidationError(w, "invalid JSON body")
		return
	}

	if req.Hostname == "" {
		writeValidationError(w, "hostname is required")
		return
	}
	if req.PrimaryMAC == "" {
		writeValidationError(w, "primary_mac is required")
		return
	}
	if req.BaseImageID == "" {
		writeValidationError(w, "base_image_id is required")
		return
	}
	req.PrimaryMAC = strings.ToLower(req.PrimaryMAC)
	if !api.IsValidNodeProvider(req.Provider) {
		writeValidationError(w, "provider must be one of: ipmi, proxmox, or empty string")
		return
	}

	// S2-9: Validate BaseImageID exists before INSERT to surface a clear 400 rather
	// than letting the SQLite FK constraint produce an opaque 500.
	if _, err := h.DB.GetBaseImage(r.Context(), req.BaseImageID); err != nil {
		writeValidationError(w, "image not found: base_image_id does not exist")
		return
	}

	// S2-4: accept both "tags" and deprecated "groups" from the request body.
	reqTags := req.Tags
	if len(reqTags) == 0 && len(req.Groups) > 0 {
		reqTags = req.Groups
	}

	now := time.Now().UTC()
	cfg := api.NodeConfig{
		ID:          uuid.New().String(),
		Hostname:    req.Hostname,
		FQDN:        req.FQDN,
		PrimaryMAC:  req.PrimaryMAC,
		Interfaces:  req.Interfaces,
		SSHKeys:     req.SSHKeys,
		KernelArgs:  req.KernelArgs,
		Tags:        reqTags,
		Groups:      reqTags, // dual-emit: mirror tags into deprecated field
		CustomVars:  req.CustomVars,
		BaseImageID: req.BaseImageID,
		Provider:    req.Provider,
		CreatedAt:   now,
		UpdatedAt:   now,
	}
	if cfg.Interfaces == nil {
		cfg.Interfaces = []api.InterfaceConfig{}
	}
	if cfg.SSHKeys == nil {
		cfg.SSHKeys = []string{}
	}
	if cfg.Tags == nil {
		cfg.Tags = []string{}
		cfg.Groups = []string{}
	}
	if cfg.CustomVars == nil {
		cfg.CustomVars = map[string]string{}
	}

	if err := h.DB.CreateNodeConfig(r.Context(), cfg); err != nil {
		log.Error().Err(err).Msg("create node config")
		writeError(w, err)
		return
	}

	if h.Audit != nil {
		aID, aLabel := "", ""
		if h.GetActorInfo != nil {
			aID, aLabel = h.GetActorInfo(r)
		}
		h.Audit.Record(r.Context(), aID, aLabel, db.AuditActionNodeCreate, "node", cfg.ID,
			r.RemoteAddr, nil, map[string]string{"hostname": cfg.Hostname, "primary_mac": cfg.PrimaryMAC})
	}

	setNodeConfigSunsetHeader(w) // S6-7: "groups" deprecated, removed in v1.1
	writeJSON(w, http.StatusCreated, cfg)
}

