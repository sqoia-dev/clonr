// UpdateNode handles PUT /api/v1/nodes/:id
func (h *NodesHandler) UpdateNode(w http.ResponseWriter, r *http.Request) {
	id := chi.URLParam(r, "id")

	var req api.UpdateNodeConfigRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		writeValidationError(w, "invalid JSON body")
		return
	}

	if req.Hostname == "" {
		writeValidationError(w, "hostname is required")
		return
	}
	if req.PrimaryMAC == "" {
		writeValidationError(w, "primary_mac is required")
		return
	}
	req.PrimaryMAC = strings.ToLower(req.PrimaryMAC)

	// Fetch to confirm existence.
	existing, err := h.DB.GetNodeConfig(r.Context(), id)
	if err != nil {
		writeError(w, err)
		return
	}

	// Validate extra mount entries before touching the database.
	for i, m := range req.ExtraMounts {
		if err := api.ValidateFstabEntry(m); err != nil {
			writeValidationError(w, fmt.Sprintf("extra_mounts[%d]: %s", i, err.Error()))
			return
		}
	}

	// Resolve the layout override:
	//   - ClearLayoutOverride=true  → explicitly remove override (set nil)
	//   - DiskLayoutOverride present in request → use the new value
	//   - Neither present (omitempty field absent) → preserve existing override
	//     so that a PUT to change hostname/image does not silently wipe a
	//     node-level disk layout that was set via the dedicated override endpoint.
	var layoutOverride *api.DiskLayout
	switch {
	case req.ClearLayoutOverride:
		layoutOverride = nil // explicit clear
	case req.DiskLayoutOverride != nil:
		layoutOverride = req.DiskLayoutOverride // caller supplied a new override
	default:
		layoutOverride = existing.DiskLayoutOverride // preserve existing
	}

	// Resolve the power provider:
	//   - ClearPowerProvider=true → explicitly remove (revert to BMC/IPMI fallback)
	//   - PowerProvider present in request → use the new config (credentials updated)
	//   - Neither present (omitempty field absent) → preserve existing config so
	//     that a PUT to change hostname/image does not silently wipe credentials.
	var powerProvider *api.PowerProviderConfig
	switch {
	case req.ClearPowerProvider:
		powerProvider = nil // explicit clear
	case req.PowerProvider != nil:
		powerProvider = req.PowerProvider // caller supplied a new provider config
	default:
		powerProvider = existing.PowerProvider // preserve existing credentials
	}

	// S6-6: group_id column dropped. GroupID changes now route through
	// AssignNodeToGroup (which writes to node_group_memberships). An empty GroupID
	// in the request means "leave as-is". To clear, use the dedicated
	// group-membership endpoint.
	desiredGroupID := existing.GroupID
	if req.GroupID != "" {
		desiredGroupID = req.GroupID
	}

	// S2-4: accept both "tags" and deprecated "groups" from the request body.
	updReqTags := req.Tags
	if len(updReqTags) == 0 && len(req.Groups) > 0 {
		updReqTags = req.Groups
	}

	// Migration 054: resolve per-node verify timeout override.
	var verifyTimeoutOverride *int
	switch {
	case req.ClearVerifyTimeoutOverride:
		verifyTimeoutOverride = nil // revert to global
	case req.VerifyTimeoutOverride != nil:
		verifyTimeoutOverride = req.VerifyTimeoutOverride
	default:
		verifyTimeoutOverride = existing.VerifyTimeoutOverride // preserve existing
	}

	cfg := api.NodeConfig{
		ID:                    id,
		Hostname:              req.Hostname,
		HostnameAuto:          false, // admin explicitly set the hostname → mark as non-auto
		FQDN:                  req.FQDN,
		PrimaryMAC:            req.PrimaryMAC,
		Interfaces:            req.Interfaces,
		SSHKeys:               req.SSHKeys,
		KernelArgs:            req.KernelArgs,
		Tags:                  updReqTags,
		Groups:                updReqTags, // dual-emit: mirror tags into deprecated field
		CustomVars:            req.CustomVars,
		BaseImageID:           req.BaseImageID,
		PowerProvider:         powerProvider,
		GroupID:               desiredGroupID,
		DiskLayoutOverride:    layoutOverride,
		ExtraMounts:           req.ExtraMounts,
		VerifyTimeoutOverride: verifyTimeoutOverride,
		CreatedAt:             existing.CreatedAt,
		UpdatedAt:             time.Now().UTC(),
	}
	if cfg.Interfaces == nil {
		cfg.Interfaces = []api.InterfaceConfig{}
	}
	if cfg.SSHKeys == nil {
		cfg.SSHKeys = []string{}
	}
	if cfg.Tags == nil {
		cfg.Tags = []string{}
		cfg.Groups = []string{}
	}
	if cfg.CustomVars == nil {
		cfg.CustomVars = map[string]string{}
	}

	if err := h.DB.UpdateNodeConfig(r.Context(), cfg); err != nil {
		log.Error().Err(err).Msg("update node config")
		writeError(w, err)
		return
	}

	// S6-6: If the group assignment changed, propagate through node_group_memberships.
	if desiredGroupID != existing.GroupID {
		if err := h.DB.AssignNodeToGroup(r.Context(), id, desiredGroupID); err != nil {
			log.Warn().Err(err).Str("node_id", id).Str("group_id", desiredGroupID).
				Msg("update node: group assignment write failed (non-fatal)")
		} else {
			cfg.GroupID = desiredGroupID
		}
	}

	// S5-12: Record field-level config change history.
	aID, aLabel := "", ""
	if h.GetActorInfo != nil {
		aID, aLabel = h.GetActorInfo(r)
	}
	if changes, diffErr := db.DiffNodeConfigFields(sanitizeNodeConfig(existing), sanitizeNodeConfig(cfg)); diffErr == nil {
		if recErr := h.DB.RecordNodeConfigChanges(r.Context(), id, aLabel, changes); recErr != nil {
			log.Warn().Err(recErr).Str("node_id", id).Msg("update node: record config history failed (non-fatal)")
		}
	}

	if h.Audit != nil {
		h.Audit.Record(r.Context(), aID, aLabel, db.AuditActionNodeUpdate, "node", id,
			r.RemoteAddr, sanitizeNodeConfig(existing), sanitizeNodeConfig(cfg))
	}

	setNodeConfigSunsetHeader(w) // S6-7: "groups" deprecated, removed in v1.1
	writeJSON(w, http.StatusOK, cfg)
}

