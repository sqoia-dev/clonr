// ComputeSchemaDriftForStartup is a lightweight wrapper used by server.go's
// startup goroutine.  Returns (true, nil) when drift is detected, (false, nil)
// when schemas are in sync, and (false, err) when the check itself fails.
func ComputeSchemaDriftForStartup() (bool, error) {
	result, err := computeSchemaDrift()
	if err != nil {
		return false, err
	}
	return result.Status == "drift", nil
}

// generateSchemasInProcess generates all schema files in-process using the
// same reflector config as cmd/generate-schemas/main.go.
// Returns a map of basename (e.g. "NodeConfig.json") → JSON bytes.
func generateSchemasInProcess() (map[string][]byte, error) {
	r := jsonschema.Reflector{
		AllowAdditionalProperties: false,
		DoNotReference:            false,
		RequiredFromJSONSchemaTags: true,
		Namer: func(t reflect.Type) string {
			return t.Name()
		},
	}

	result := make(map[string][]byte)
	seen := make(map[string]bool)

	for _, t := range schemaTypesForDrift {
		name := t.Name()
		if seen[name] {
			continue
		}
		seen[name] = true

		schema := r.ReflectFromType(t)
		out, err := json.MarshalIndent(schema, "", "  ")
		if err != nil {
			return nil, fmt.Errorf("marshal %s: %w", name, err)
		}
		result[name+".json"] = append(out, '\n')
	}

	return result, nil
}
